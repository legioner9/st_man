 АВТОРЫ 

Изначально драйвер принтера был описан Jim Weigand и Linus
Torvalds.  Далее он был доработан Michael K. Johnson.  Программа прерывания
написана Nigel Gamble.  Alan Cox сделал ее модульной.
LPCAREFUL, LPABORT, LPGETSTATUS были добавлены Chris Metcalf.
   
