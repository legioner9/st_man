 ПРИМЕЧАНИЯ 
 Помните, что для поддержки режимов UDMA4/ATA66 и выше, необходимо использовать
кабели из 80 проводников. Проверьте, что шлейфы - не длиннее 45 сантиметров. Для
круглых кабелей ATA длина зависит от качества кабеля. Кабели SATA, в соответствии со
спецификацией, могут быть длиной до 1 метра. 

 Статическая нумерация устройств (которая включается с помощью опции ядра
 ATA_STATIC_ID ) резервирует номер для каждого диска, который может быть
потенциально подключен, даже если его нет. Это удобно для горячей замены,
когда диски должны всегда представляться устройством с одним и тем же номером,
независимо от порядка подключения. 

