 НАЗВАНИЕ 

getent - получает элементы из административной базы данных
   
 СИНТАКСИС 

 getent   database  [ key  ...]
   
 ОПИСАНИЕ 

Программа  getent  собирает элементы из административных
баз данных, используя указанные ключи поиска.
Здесь  database  может быть aliases, ethers, group, hosts,
netgroup, networks, passwd, protocols, rpc, services или shadow.
   
