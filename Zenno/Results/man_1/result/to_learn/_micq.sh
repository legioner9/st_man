 ИСПОЛЬЗОВАНИЕ 

После первого запуска (для отдельно взятой базовой папки) mICQ запросит
UIN либо создание нового. После первого входа будет автоматически
импортирован список контактов с сервера.
 

Базовые команды включают в себя
 msg 

сообщение другим контактам (нажатия клавиши <tab> на пустой строке
ввода вызовут команды msg тем контактам, с которыми до того велись
разговоры),
 add 

добавление новых контактов
 update ,  other , and  about 

изменение своих мета-данных на сервере ICQ и, конечно,
 help 

интерактивную справку. Полный список команд доступен на справочной
странице
 micq (7) . 

   
 ВОЗМОЖНОСТИ 

 mICQ 

импортирован на множество платформ и работает в системах Linux, DOS,
OS/2, Win32, BeOS, AmigaOS, GNU HURD и многих других современных
unix-системах.
 mICQ версии 0.5 

достоверно работает в системах Linux, AIX, HPUX, Tru64, AmigaOS,
частично BeOS и других unix-системах. Однако другие порты могут
работать, а могут и не работать, и любое тестирование этого
приветствуется.
   
