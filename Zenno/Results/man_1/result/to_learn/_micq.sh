 ОПИСАНИЕ 

 mICQ 

это текстовый ICQ-клиент, поддерживающий многие возможности ICQ,
предоставляемые клиентом от Mirabilis. mICQ позволяет менять пароль
пользователя, создавать новые учётные записи, обновлять информацию
о пользователе, искать других пользователей, отправлять SMS, открывать
прямые соединения, отправлять сообщения с подтверждённой доставкой,
передавать файлы, опознавать программу-клиент собеседника, обмениваться
сообщениями в кодировке UTF-8, исполнять внешние команды в качестве
реакции на любое событие, и так далее.
   
