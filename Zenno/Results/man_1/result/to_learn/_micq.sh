 ФАЙЛЫ 

 
 ~/.micqrc 

 
Старый файл конфигурации для mICQ - устарел.
 ~/.micq/ 

 
Стандартная базовая папка, где mICQ ищет свои файлы (BASE).
 BASE/micqrc 

 
Файл настроек для mICQ. Содержит общую конфигурацию.
 BASE/status 

 
Файл настроек для mICQ. Содержит список контактов и
настройки контактов.
 BASE/i18n/*.i18n 

 
Локальные замены для файлов переводов mICQ.
 BASE/contacts/ 

 
Папка, где хранятся мета-данные по контактам.
 BASE/history/ 

 
Стандартная папка для файлов журнала.
 BASE/files/ 

 
Папка, где сохраняются входящие переданные файлы.
 BASE/remote-control 

 
Старое стандартное размещение сокета FIFO, используемого
при применении скриптов. Сокет был переименован, поскольку
вводил пользователей в заблуждение.
 BASE/scripting 

 
Стандартное размещение сокета FIFO, используемого при
применении скриптов.
 PREFIX/share/micq/*.i18n 

 
Файлы локализации, которые содержат переводы всех строк
вывода mICQ.
 
   
