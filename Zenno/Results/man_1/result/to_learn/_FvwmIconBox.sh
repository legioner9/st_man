 НАЗВАНИЕ 

 
FvwmIconBox - модуль FVWM iconbox
 
   
 СИНТАКСИС 

 
FvwmIconBox вызывается оконным менеджером fvwm и не будет работать при
вызове его из командной строки.
 
   
 ОПИСАНИЕ 

 
Модуль FvwmIconBox предоставляет менеджер иконок. С
помощью клавиатуры или мыши пользователь может выполнять
такие операции, как свертка окна в иконку и развертка
обратно, для каждой иконки, которую покажет модуль.
 
FvwmIconBox читает тот же самый файл .fvwmrc, который
использует и fvwm при старте, и обрабатывает строки, начинающиеся
с приставки *FvwmIconBox, в которых содержатся его
конфигурационные параметры. Например, ``*FvwmIconBoxFore green''.
 
   
