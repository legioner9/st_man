 НАЗВАНИЕ 
groupmod - изменение определения группы в системе

 СИНТАКСИС 
 
   
groupmod [-g  идентификатор  [-o]] [-n  имя ]  группа 
   
 


 ОПИСАНИЕ 
 Команда  groupmod  изменяет определение указанной группы путем изменения 
соответствующей записи в файле  /etc/group .

 Поддерживаются следующие опции: 

 
 -g идентификатор 
 
Идентификатор новой группы. Этот идентификатор 
группы должен быть неотрицательным десятеричным 
целым числом, не превышающим значения  MAXUID , 
определенного в заголовочном файле  param.h . По 
умолчанию выделяется уникальный идентификатор 
группы в допустимом диапазоне. Идентификаторы групп 
в диапазоне 0-99 зарезервированы. 
 -o 
 
Эта опция позволяет задавать дублирующийся (не 
уникальный) идентификатор группы. 
 -n имя 
 
Строка печатных символов, задающая новое имя для 
группы. Она не должна содержать двоеточия ( : ) или 
переводы строк ( \n ). 
 группа 
 
Текущее имя изменяемой группы. 
 

 Если перед  группой  стоит символ  + , (например,  +student ), определение группы 
администрируется сетевой информационной службой (Network Information 
Service - NIS). В этом случае, значение идентификатора группы берется из базы 
данных NIS. Для таких групп использование опций  -g  или  -o  вызовет выдачу 
сообщения о синтаксической ошибке. Более детальную информацию можно 
найти на странице справочного руководства  group(4) . 

