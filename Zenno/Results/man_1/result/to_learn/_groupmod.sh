 ДИАГНОСТИКА 

 Команда  groupmod  завершается с кодом возврата 0 в случае успеха или выдает
сообщения об ошибках в следующих случаях: 

 
 Неверный синтаксис команды.  
 Invalid command syntax. 
 
 В опции задан неверный аргумент.  
 An invalid argument was provided to an option. 
 
 Идентификатор группы не уникален (когда не использована опция -o).  
 gid is not unique (when the -o option is not used). 
 
 группа  не существует.  
 group does not exist. 
 
 имя уже используется в качестве имени группы.  
 name already exists as a group name. 
 
 Не удалось изменить файл /etc/group.  
 Cannot update the /etc/group file. 
 

