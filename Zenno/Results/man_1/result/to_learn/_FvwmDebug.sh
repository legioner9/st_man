 Инициализация 

 
Во время инициализации FvwmDebug требует от fvwm
передачи по конвейеру полного списка окон. Этот список
завершается пакетом с сообщением ``end Send_WindowList''.
 
   
 Вызов 

 
FvwmDebug может быть вызван вставкой строки `Module
FvwmDebug' в в файл .fvwm95rc. Данная строка может быть
добавлена в функцию инициализации для вызова этого модуля
при инициализации fvwm95 или привязана к нажатию кнопки
мыши или к какой-либо горячей клавише. fvwm95
ищет этот модуль в каталоге, указанном в ModulePath.
 
   
