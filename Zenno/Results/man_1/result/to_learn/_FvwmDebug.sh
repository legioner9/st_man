 НАЗВАНИЕ 

 
FvwmDebug - модуль-отладчик FVWM
 
   
 СИНТАКСИС 

 
FvwmDebug вызывается оконным менеджером  fvwm  и не
может быть вызван из командной строки.
 
   
 ОПИСАНИЕ 

 
FvwmDebug захватывает поток пакетов модулей, исходящий от
 fvwm95 , и отображает его на стандартный вывод диагностики (stderr).
 
   
