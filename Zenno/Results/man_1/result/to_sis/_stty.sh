 NAME 
 stty - печатает или изменяет параметры терминала
   
 СИНТАКСИС 

 stty 

[ -F УСТРОЙСТВО ] [ --file=УСТРОЙСТВО ] [ ПАРАМЕТР ]...
 

 stty 

[ -F УСТРОЙСТВО ] [ --file=УСТРОЙСТВО ] [ -a|--all ]
 

 stty 

[ -F УСТРОЙСТВО ] [ --file=УСТРОЙСТВО ] [ -g|--save ]
   
 ОПИСАНИЕ 


 

Печатает или изменяет параметры терминала.
 
 -a ,  --all 
напечатать все текущие установки в форме, понятной человеку
 -g ,  --save 
напечатать все текущие установки в форме, понятной stty
 -F ,  --file = УСТРОЙСТВО 
открыть и использовать указанное УСТРОЙСТВО вместо стандартного ввода
 --help 
показать справку и выйти
 --version 
показать информацию о версии и выйти
 
 

Необязательный символ `-' перед ПАРАМЕТРОМ означает отрицание.  Символом `*' отмечены не описанные в POSIX
параметры.  Доступность того или иного параметра определяется используемой системой.
   
 Специальные символы: 

 
 * dsusp СИМВОЛ 
СИМВОЛ будет посылать терминалу сигнал останова при завершении ввода
 eof СИМВОЛ 
СИМВОЛ будет означать конец файла (прекратить ввод)
 eol СИМВОЛ 
СИМВОЛ будет означать конец строки
 * eol2 СИМВОЛ 
другой СИМВОЛ для конца строки
 erase СИМВОЛ 
СИМВОЛ будет стирать последний введённый до него
 intr СИМВОЛ 
СИМВОЛ будет посылать сигнал прерывания
 kill СИМВОЛ 
СИМВОЛ будет стирать текущую строку
 * lnext СИМВОЛ 
СИМВОЛ будет вводить следующий символ, отменяя его специальное действие
 quit СИМВОЛ 
СИМВОЛ будет посылать сигнал выхода
 * rprnt СИМВОЛ 
СИМВОЛ будет перерисовывать текущую строку
 start СИМВОЛ 
СИМВОЛ будет возобновлять ввод
 stop СИМВОЛ 
СИМВОЛ будет приостанавливать ввод
 susp СИМВОЛ 
СИМВОЛ будет посылать терминалу сигнал останова
 * swtch СИМВОЛ 
CHAR будет переключать уровень вложенности оболочки
 * werase СИМВОЛ 
CHAR будет стирать последнее введённое слово
 
   
 Специальные установки: 

 
