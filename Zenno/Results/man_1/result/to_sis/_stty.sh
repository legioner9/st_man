Управляет терминальной строкой, подключённой к стандартному выводу. Запущенная без аргументов,
печатает скорость передачи информации, порядок передачи информации, и отличия от обычных установок.
При установке, СИМВОЛ трактуются буквально или задаются как ^c, 0x37, 0177 или
127; Для отмены специальных символов служат значения ^- или undef.
   
