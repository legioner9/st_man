 НАЗВАНИЕ 
 groupdel  - удаление определения группы из системы 

 СИНТАКСИС 
 
   
groupdel  группа 
   
 

 ОПИСАНИЕ 
 Команда  groupdel  удаляет определение группы из системы путем удаления 
записи о соответствующей группе из файла  /etc/group . Она, однако, не удаляет 
идентификатор группы (GID) из файла паролей; удаленный GID действует для 
всех файлов и каталогов, которые его имели. 

 Поддерживаются следующие опции: 

 
 группа 
 
Строка печатных символов, задающая удаляемую группу.
 
 

