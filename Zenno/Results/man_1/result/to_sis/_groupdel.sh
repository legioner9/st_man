 ДИАГНОСТИКА 
 Команда  groupdel  завершается с кодом возврата 0 в случае успеха или выдает 
сообщения об ошибках в следующих случаях: 

 
 Группа не существует.  
 group does not exist. 
 
 Не удалось изменить файл /etc/group.  
 Cannot update the /etc/group file. 
 

