 ОШИБКИ И ОГРАНИЧЕНИЯ 

Атрибуты `c', 's' и `u' пока не работают в файловых системах ext2 и ext3,
хотя уже в текущей линейке ядер Linux они поддерживаются и их можно было бы использовать.
Эти атрибуты могут быть реализованы в будущих версиях ext2 и ext3.
 

Опция `j' полезна только для смонтированной файловой системы ext3.
 

Опция `D' полезна только для ядра Linux 2.5.19 или выше.
   
