 gliv (1) >>  gliv  (1)   ( Русские man: Команды и прикладные программы пользовательского уровня ) 
   
 

gliv - Программа просмотра изображений с поддержкой OpenGL
   
 

 gliv  [OPTIONS]... [FILES]...
   
 

 gliv  использует gdk-pixbuf для загрузки изображений и OpenGL для отображения их на экране. Во время просмотра можно прокручивать, вращать и увеличивать/уменьшать изображения, а так же осуществлять просмотр в режиме слайд-шоу.
   
 

После запуска программы все параметры устанавливаются в значения по умолчанию (значение "off" для флагов), затем считывается ~/.glivrc или /etc/glivrc или файл конфигураций, указанный в командной строке, и, наконец, параметры командной строки.
 

Для опций, которые могут принимать значения "on|off" аргумент можно опускать, то есть --foo аналогична --foo=on.
 
 -h, --help 
Вывести справку и заверщить работу.
 -V, --version 
Вывести версию программы и завершить работу.
 -a, --add-all[=on|off] 
Добавить все файлы в каталоге. Если указан этот флаг, то при указании одного файла в командной строке будут загружены все файлы в каталоге.
 -R, --recursive[=on|off] 
Рекурсивно просматривать каталоги. Если Вы укажите каталог в командной строке,  gliv  будет просматривать все изображения из этого каталога и всех его подкаталогов.
 -S, --sort[=on|off] 
Показывать изображения в отсортированном порядке. Перед началом слайд-шоу список изображений будет отсортирован.
 -s, --shuffle[=on|off] 
Показывать изображения в произвольном порядке. Перед началом слайд-шоу список изображений перемешивается.
 -F, --force-load[=on|off] 
Пытаться загрузить все файлы. При загрузке изображений программа выбирает способ загрузки в соответствии с расширением файла для ускорения работы. Если расширение неизвестно, файл игнорируется. Данный параметр заставляет  gliv  всегда пытаться загрузить изображение.
 -C, --client[=on|off] 
Соедениться с уже запущенным  gliv -сервером. С этой опцией  gliv  будет открывать файлы, переданные в аргументах  gliv -сервера. В этом режиме доступны только опции сортировки, перемешивания и рекурсивной загрузки каталогов. Кроме того возможно использование опции -0.  gliv -сервер это последняя запущенная копия  gliv  или копия, в которой режим сервера выбран в меню Параметры.
 -e, --build-menus[=on|off] 
Не создавать меню изображений при загрузке программы. Отказ от создания меню ускоряет процесс запуска програмы, особенно если в командной строке указано много файлов.
 -g, --glivrc[=FILE] 
Укажите этот параметр чтобы отказаться от загрузки файла настроек ~/.glivrc. Если в параметре указано имя файла, этот файл будет использоваться как конфигурационный. 
 -w, --slide-show[=on|off] 
Начинать слайд-шоу сразу после загрузки программы. С этим параметром Вам не надо запускать слайд-шоу из меню вручную.
 -0, --null[=on|off] 
Читать имена файлов, разделенные нулевым символом (null-terminated filenames). Вы можете использовать эту опцию совместно с "find -print0" или с "tr '\n' '\0'" когда имеется очень длинный список имен файлов, который надо передать в  gliv . В отличии от использования  xargs (1) это позволяет передать неограниченное число имен файлов.
 -o, --collection[=FILE] 
Записать коллекцию. При использовании этого параметра  gliv  создает коллекцию из загруженных файлов и записывает ее в поток стандартный вывода или в указанный файл.
 -G, --geometry=GEOMETRY 
Initial window geometry. This option can be used to specify the position and dimension of the  gliv  window. It expects a geometry argument in the  XParseGeometry (3X11) format like: 640x480+20-30 for example.
 
   
