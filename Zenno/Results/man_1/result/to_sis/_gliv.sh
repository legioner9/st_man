Первая кнопка мышки и клавиши-стрелки двигают изображение. При нажатой клавише Control они вращают изображение относительно центра.
 

 
Колесо прокрутки на мышке (если оно поддерживается) изменяет размеры изображение, а одновременно с нажатой средней кнопкой меняет изображение.
 

 
Изменить размеры изображения можно двигая мышкой вертикально одновременно с нажатыми клавишей Shift и первой кнопкой мышки.
 

 
Клавиши Space (пробел) и Backspace (возврат курсора) действуют аналогично клавишам n и p.
 

 
Выделите прямоугольник третьей (правой) кнопкой мышки и Gliv увеличит отмеченную область.
 

 
   
