	 НАЗВАНИЕ ¶ 


qatom - сравнение строк атомов 

	 СИНТАКСИС ¶ 


	 qatom   <опции> <пакет> 


	 ОПИСАНИЕ ¶ 


	 Доступные опции:  -[cvqChV] 


	 -c ,  --compare 


	 * Сравнить два атома 


	 -v ,  --verbose 


	 * Предлагать подробный вывод 


	 -q ,  --quiet 


	 * Предлагать компактный вывод - в частности, не выводить предупреждения 


	 -C ,  --nocolor 


	 * Не использовать цветной вывод 


	 -h ,  --help 


	 * Вывести эту справку и выйти 


	 -V ,  --version 


