 xdtv_alevt-date (1) >>  xdtv_alevt-date  (1)   ( Русские man: Команды и прикладные программы пользовательского уровня ) 
   
 NAME 
 xdtv_alevt-date - display/set time received via Teletext (adapted to xdTV)

   
 SYNOPSIS 

 xdtv_alevt-date 

[ options ]


   
 ОПИСАНИЕ 

 xdtv_alevt-date 

показывает время, полученное с источника телетекста.  Оно может быть использовано для установки
системного времени.  Дата не интерпретируется (даже не
передается на большинстве каналов).  Поэтому возможна только установка +/-12 часов.
Установка по умолчанию ограничена +/-2 часами (используйте -delta чтобы изменить). 
Без опции -set дата показывается в формате
команды
 date (1)



   
 ОПЦЫИ 

Сумма опций представлена ниже.
 
 -set 

 
Установить системное время, используя полученное с Teletext.
 -delta  seconds 

 
Макс. установка времени для системных часов.  По умолчанию
7200 секунд (2 часа) и максимальное 12 часов.
 -format  string 

 
Формат выдачи времени.  См.
 strftime (3)

для возможных опций.
 -vbi  device 

 
Использовать указанное устройство (по умолчанию: /dev/vbi).
Для DVB можно использовать /dev/dvb/adapter0/demux0 
(для использования первой DVB карты или
/dev/dvb/adapter1/demux0 для второй, и т.д.)
 -progname  thechannelname -pid  number 

 
Для DVB, указывает какая программа (укажите ее имя)
будет обрабатывать телетекст & и ее PID.
 -timeout  seconds 

 
Если время не может быть определено в
 секундах ,

программа завершается с SIGALRM.
 -fvbio  или -nfvbio 

 
Вкл/выкл фикс смещения vbi. Эта ошибка возникает
на ядрах >=2.6.16.  Обычно включен.
 --help 

 
Показать все опции.
 --version 

 
Версия программы.
 
 

Перед стартом программы, вы должны указать канал с помощью
другой программы вроде
 xdtv 

 

Заметьте: Эта программа
 не  

устанавливает время запасных батарей вашего ПК.
 clock -w 

сделает это.

   
 FILES 

 /dev/vbi* 


   
 СМ. ТАКЖЕ 

 xdtv_alevt (1x),

 xdtv_alevt-cap (1),

 strftime (3),

 date (1),

