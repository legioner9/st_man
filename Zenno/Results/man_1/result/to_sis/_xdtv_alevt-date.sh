 ГЛЮКИ 

Эта программа просто для баловства.  Время, передаваемое ТВ станциями,
более чем неточное.  Некоторые находятся в пределах вашего местного времени,
в то время как другие вылетают более чем на 15 минут.  Я вас предупредил.
(И не думайте, что pkt8/30 время - лучше.  Это даже хуже.)
 

