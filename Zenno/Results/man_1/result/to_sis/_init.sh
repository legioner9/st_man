 ДИАГНОСТИКА 

 Если процесс  init  обнаруживает, что перезапускает запись из файла  /etc/inittab 
более десяти раз за две минуты, предполагается, что в командной строке этой записи есть
ошибка, и об этом выдается сообщение на системную консоль. Затем эта запись больше не будет
запускаться повторно,пока не пройдет пять минут или не будет получен сигнал от запущенного
пользователем процесса  init  или  telinit . Это предотвращает напрасное использование
ресурсов системы процессом  init  в случае опечатки в файле  inittab  или
удаления программы, на которую ссылается запись в файле  /etc/inittab . 

 ПРИМЕЧАНИЯ 

 Процессы  init  и  telinit  может запускать только привилегированный пользователь. 

 Следует избегать бессистемного использования уровней выполнения  S  и  s  в
файле  /etc/inittab . При изменении этого файла, лучше избегать добавления этого состояния
в любые строки, кроме  initdefault . 

 Если стандарное состояние не указано с помощью записи  initdefault  в файле 
 /etc/inittab , система переходит на уровень 6. Как следствие, система зациклится и
будет постоянно перезагружаться. 

 Если при загрузке системы не удается создать файл  utmpx , система загрузиться в
состояние " s ", независимо от состояния, указанного в записи  initdefault 
файла  /etc/inittab . Это может произойти при недоступности файловой системы  /var . 

