 ФАЙЛЫ 

 
 ~/.scmxx/cache.pb 
этот файл служит при выводе короткого сообщения (для подмены адресов
получателя и отправителя) и при отправке короткого сообщения для
подмены номера в опции --number. Формат файла совпадает с форматом
файла для телефонной книги (номера слотов не имеют значения, но
должны присутствовать).
 ~/.scmxx/config 
этот файл может содержать набор длинных опций (без --), аргументы
отделяются от названия опции символом '='. Любое количество
пробельных символов допускается вначале строки, перед и после
разделителя. Символ '#' вначале строки помечает её как комментарий.
Примеры:
 
 
          #выбрать устройство доступа
          device  =  /dev/ttyS0
          #всегда отправлять SMS с использованием UCS-2
          unicode
          
 

 
 ~/.scmxx/pin 
Данный файл используется как алтернатива опции командной
строки --pin. Файл НЕ должен быть доступен группе или всем на
чтение/запись! Также он ДОЛЖЕН быть обычным файлом, не
символической ссылкой. SCMxx отвергает файлы в подобных
случаях. Если значение PUK-кода запрашивается телефоном,
соответствующий PIN-код должен быть также определён. Только
такие элементы форматирования являются необходимыми: '{',
'=', ';' и '}'. Пробелы и символы новой строки игнорируются.
Файл имеет следующий формат:
 
 
          sim 262031234567890 {
            pin = 1234
            puk = 12345678;
            pin2 = 4321;
            puk2 = 87654321;
          }
          device 350123456789012 {
            type SIM {
              pin = 0000;
              puk = 0000;
            }
          }
          
 

 
Секции "sim" используют IMSI в качестве идентификатора, секции
же "device" уже используют IMEI как идентификатор (см. вывод
опции --info). Следовательно IMSI необходим, так как без него
НЕЛЬЗЯ включить телефон!
Подсекция "type" в секции device определяет следующие идентификаторы:
 
 
 SIM 
код устройства (защита от кражи)
 FSIM 
самая первая установленная SIM-карта
 NET 
привязка к сети
 NETSUB 
дополнительная привязка к сети
 SP 
привязка к оператору связи
 CORP 
привязка к корпорации
 
 

 
 
   
