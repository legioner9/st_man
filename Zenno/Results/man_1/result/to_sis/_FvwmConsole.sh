 ВЫЗОВ 

 
FvwmConsole может быть вызван вставкой строки `Module
FvwmConsole' в файл .fvwm95rc. Если поместить эту строку в
InitFunction, то FvwmConsole будет вызываться во время
инициализации, но также можно привязать вызов к пункту
меню или действию мыши, что позволяет запустить его в любой момент.
 fvwm95  ищет FvwmConsole в каталоге, заданном
параметром ModulePath.
 
   
 ОПЦИИ КОНФИГУРАЦИИ 

 
FvwmConsole использует для работы xterm. Все ресурсы, установленные
для xterm, наследуются, за исключением тех, которые переопределены
опциями командной строки.
 
   
