 FvwmConsole (1) >>  FvwmConsole  (1)   ( Русские man: Команды и прикладные программы пользовательского уровня ) 
 
   
 НАЗВАНИЕ 

 
FvwmConsole - интерфейс для ввода команд FVWM
 
   
 СИНТАКСИС 

 
FvwmConsole вызывается оконным менеджером  fvwm95  из меню или файла .fvwm95rc
и не может быть вызван из командной строки. FvwmConsole
воспринимает все опции xterm за исключением -e, -name и
-title.
 
 
Module FvwmConsole -g 40x10 -fg black -bg green3

 

 
Также в файле .Xdefaults могут быть заданы X ресурсы.
 
 
FvwmConsole*geometry: 40x4
FvwmConsole*font: 7x14

 

 
   
 ОПИСАНИЕ 

 
FvwmConsole позволяет пользователю вводить команды  fvwm95 
в xterm и немедленно их исполнять. Эти команды обычно
определяются в файле .fvwm95rc или привязываются к меню
или событиям мыши, как определено в этом файле. Этот модуль
особенно полезен для тестирования новых конфигурационных
идей или реализации временных изменений в вашем окружении.
 
Для редактирования командой строки вызывается GNU readline
library. Как альтернативный редактор команд может также
использоваться сценарий perl. Его команды похожи на emacs.
 
 
