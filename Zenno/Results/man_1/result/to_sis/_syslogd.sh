 Пример 2: результат работы syslogd при включенной генерации идентификаторов сообщений и записи в журнальный файл /var/adm/messages 

 Следующий пример показывает результат работы демона  syslogd , когда генерация
идентификаторов сообщений включена. Учтите, что идентификатор сообщения записывается в
журнальный файл  /var/adm/messages . 

