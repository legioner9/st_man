 НАЗВАНИЕ 
 syslogd  - регистрация системных сообщений 

 СИНТАКСИС 
 
/usr/sbin/syslogd [ -d ] [ -f  файл_конфигурации  ] [ -m  интервал_отметок  ] 
  [ -p  путь  ] [ -t ]
 

 ОПИСАНИЕ 

 Демон  syslogd  принимает системные сообщения и направляет их в соответствующие
журнальные файлы или соответствующим пользователям, в зависимости от приоритета
сообщения и компонента системы, от которого оно получено. Файл конфигурации
 /etc/syslog.conf  (см.  syslog.conf(4) )
задает, куда направляются сообщения. Демон  syslogd  записывает контрольное
сообщение (временную отметку) каждые  интервал_отметок  минут (по умолчанию - 20)
с приоритетом  LOG_INFO  на устройство, имя которого задано в записи для компонента
 mark  в файле  syslog.conf . 

 Системное сообщение состоит из строки текста, перед которой может идти код приоритета
в угловых скобках (<>); коды приоритетов задаются в заголовочном файле
 <sys/syslog.h> . 

 Демон  syslogd  читает сообщения из специального устройства STREAMS, 
 /dev/log , а также из любого устройства, представляющего транспортный протокол, заданного в файлах  /etc/netconfig ,
 /etc/net/transport/hosts  и  /etc/net/transport/services . 

 Демон  syslogd  читает файл конфигурации при запуске, и перечитывает при
получении сигнала  HUP  (см.  signal(3HEAD) ).
При получении этого сигнала он закрывает все открытые файлы, перечитывает файл конфигурации и открывает только те журнальные файлы, которые в нем указаны. Демон 
 syslogd  завершает работу при получении сигнала  TERM . 

 При запуске демон  syslogd  создает файл  /etc/syslog.pid ,
если это возможно, содержащий его идентификатор процесса ( PID ). 

 Если включена генерация идентфикаторов сообщений (см. 
 log(7D) ), перед каждым сообщением будет идти идентфикатор
в формате : [ ID   msgid   компонент . приоритет ]. 
 msgid  - это числовой идентфикатор сообщения, описанный на странице 
 msgid(1M) . Поля  компонент  и  приоритет 
описаны на странице  syslog.conf(4) .
 [ID 123456 kern.notice]  - пример идентификатора при включенной генерации
идентификаторов сообщений. 

 Если сообщение выдается загружаемым модулем ядра или драйвером, вместо  unix 
будет выдаваться имя модуля ядра (например,  ufs ). Примеры результатов
работы демона  syslogd  при включенной и отключенной генерации идентификаторов
сообщений см. далее в разделе  ПРИМЕРЫ . 

 Чтобы уменьшить объем выдаваемой информации, идентификаторы сообщений на консоль не выдаются, - они поступают только в журнальный файл. См.  ПРИМЕРЫ . 

 ОПЦИИ 

 Поддерживаются следующие опции: 

 
 -d 
Включает отладку. Эту опцию надо использовать только интерактивно в командном
интерпретаторе пользователя  root , как только система перешла в
многопользовательский режим. Ее нельзя задавать в сценариях запуска, поскольку это
приведет к зависанию системы в момент запуска демона  syslogd .
 
 -f файл_конфигурации 
Задает нестандартный файл конфигурации.
 
 -m интервал_отметок 
Задает интервал, в минутах, между сообщениями  mark .
 
 -p путь 
Задает полное имя альтернативного специального файла журнального устройства. По
умолчанию используется  /dev/log .
 
 -t 
Отключает порт  UDP  демона  syslogd , чтобы запретить регистрацию удаленных сообщений. 
 

