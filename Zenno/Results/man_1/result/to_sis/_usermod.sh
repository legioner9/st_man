 Недостаточно места для переноса начального каталога (опция -m). Другие 
запрошенные изменения будут выполнены.  
 Insufficient space to move the home directory (-m option). Other update 
requests will be implemented. 
 
 Не удалось выполнить перенос начального каталога в новый каталог.  
 Unable to complete the move of the home directory to the new home directory. 
 
