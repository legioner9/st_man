 The uid given with the -u option is already in use. 
 
 Изменяемое регистрационное имя не существует или группа не 
существует.  
 The login to be modified does not exist or group does not exist. 
 
 Изменяемое регистрационное имя используется.  
 The login to be modified is in use. 
 
