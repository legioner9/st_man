 НАЗВАНИЕ 

dpkg -- менеджер пакетов Debian
   
 СИНТАКСИС 

 dpkg  [ параметры ]  действие 
   
 ПРЕДУПРЕЖДЕНИЕ 

Это руководство предназначено для пользователей, желающих более подробно
ознакомиться с параметрами командной строки  dpkg  и состояниями пакетов:
здесь это описано более детально, чем в  dpkg --help .
 
Это руководство  не  следует использовать сопровождающим пакетов, которые
хотят понять, как  dpkg  будет устанавливать их пакеты. Описание того, что
делает  dpkg  при установке и удалении пакетов особенно не полно.
   
 ОПИСАНИЕ 

 dpkg  is a tool to install, build, remove and manage Debian packages. The
primary and more user-friendly front-end for  dpkg  is
 dselect (1).  dpkg  itself is controlled entirely via command line
parameters, which consist of exactly one action and zero or more
options. The action-parameter tells  dpkg  what to do and options control
the behavior of the action in some way.
 
 dpkg  can be also be used as a front-end to  dpkg-deb (1).  The following
are  dpkg-deb  actions, and if they are encountered,  dpkg  just runs
 dpkg-deb  with the parameters given to it:
      -b ,  --build ,
     -c ,  --contents ,
     -I ,  --info ,
     -f ,  --field ,
     -e ,  --control ,
     -x ,  --extract ,
     -X ,  --vextract  и
     --fsys-tarfile .
 

Все эти действия описаны в  dpkg-deb (1).
   
 ИНФОРМАЦИЯ О ПАКЕТАХ 

 dpkg  управляет некоторой полезной информацией о доступных пакетах. Эта
информация делится на три класса:  состояния ,  выбранные состояния  и
 флаги . Предполагается, что в большинстве случаев эта информация
изменяется с помощью  dselect .
   
 СОСТОЯНИЯ ПАКЕТА 

 
 installed 
Пакет распакован и успешно настроен.
 half-installed 
Установка пакета была запущена, но не закончена по какой-либо причине.
 not-installed 
Пакет в системе не установлен.
 unpacked 
Пакет распакован, но не настроен.
 half-configured 
Пакет распакован и настройка была запущена, но пока ещё не завершена по
какой-либо причине.
 config-files 
В системе остались только конфигурационные файлы пакета.
 
   
 ВЫБРАННЫЕ СОСТОЯНИЯ ПАКЕТА 

 
 install 
Пакет выбран для установки.
 deinstall 
Пакет выбран для удаления (т.е. будут удалены все файлы, кроме
конфигурационных).
 purge 
Пакет выбран для полной очистки (т.е. будут удалены абсолютно все файлы,
включая конфигурационные).
 
   
 ФЛАГИ ПАКЕТА 

 
 hold 
Пакет помечен как  зафиксированный  и не обрабатывается  dpkg , только
если не указан параметр  --force-hold .
 reinst-required 
Пакет, помеченный как  reinst-required , является сломанным и требует
переустановки. Такие пакеты не могут быть удалены, пока не указан параметр
 --force-reinstreq .
 
   
 ДЕЙСТВИЯ 

 
 dpkg -i  |  --install   имя файла пакета ... 
Установить заданный пакет. Если указан параметр  --recursive  или  -R , то
вместо  имени файла пакета  должен быть указан каталог.
 
Установка состоит из следующих шагов:
 

 
 1.  Извлечение управляющих файлов нового пакета.
 

 
 2.  Если другая версия этого пакета была установлена ранее, то запускается
сценарий  prerm  из старого пакета.
 

 
 3.  Запускается сценарий  preinst , если он есть в пакете.
 

 
 4.  Распаковываются новые файлы, в тоже самое время сохраняя старые, чтобы
если что-то пойдет не так, можно было их восстановить.
 

 
 5.  Если ранее была установлена другая версия этого пакета, то запускается
сценарий  postrm  для старого пакета. Заметим, что этот сценарий
запускается после сценария  preinst  нового пакета, потому что новые файлы
записываются в то же самое время, когда удаляются старые.
 

 
 6.  Настройка пакета. Смотрите описание  --configure , где приведена
подробная информация об этом шаге.
 dpkg --unpack  имя файла пакета ... 
Распаковать пакет, но не настраивать. Если указан параметр  --recursive 
или  --R , то вместо  имени файла пакета  должен быть указан каталог.
 dpkg --configure  имя пакета  ... |  -a  |  --pending 
Повторно настроить распакованный пакет. Если вместо  имени пакета  задан
параметр  -a  или  --pending , то все распакованные, но не настроенные
пакеты будут настроены.
 
Настройка состоит из следующих шагов:
 

 
 1.  Распаковка конфигурационных файлов, в то же самое время сохраняя
старые конфигурационные файлы, чтобы можно было восстановить их, если что-то
пойдет не так.
 

 
 2.  Запуск сценария  postinst , если он есть в пакете.
 dpkg -r  |  --remove  |  -P  |  --purge  имя пакета  ... |  -a  |  --pending 
Удалить установленный пакет. Если указан параметр  -r  или  --remove  то
удаляется всё, кроме конфигурационных файлов. Это позволяет избежать
переконфигурации пакета, если он будет повторно установлен у
будущем. (Конфигурационные файлы -- это те, что указаны в управляющем файле
 debian/confiles ). Если указан параметр  -P  или  --purge  то удаляется
всё, включая конфигурационные файлы. Если вместо  имени пакета  задан
параметр  -a  или  --pending,  то все распакованные пакеты, но отмеченные
в файле  /var/lib/dpkg/status  для удаления или полной очистки,
соответственно, удаляются или полностью очищаются.
 
Удаление пакета состоит из следующих шагов:
 

 
 1.  Запуск сценария  prerm 
 

 
 2.  Удаление установленных фалов
 

 
 3.  Запуск сценария  postrm 
 

 dpkg --update-avail  |  --merge-avail   I <файл Packages> 
Обновить информацию  dpkg  и  dselect  о доступных пакетах. Действие
 --merge-avail  комбинирует старую информацию с информацией из  файла
Packages . Действие  --update-avail  перезаписывает старую информацию
информацией из  файла Packages .  Файл Packages  распространяется с Debian
под именем  Packages .  dpkg  хранит информацию о доступных пакетах в
файле  /var/lib/dpkg/available .
 
Для упрощения процедуры получения и обновления файла  available  можно
использовать команду  dselect update .
 dpkg -A  |  --record-avail   имя файла пакета  ... 
Обновить информацию  dpkg  и  dselect  о доступных пакетах информацией из
пакета  имя файла пакета . Если задан параметр  --recursive  или  -R , то
вместо  имени файла пакета  должен быть указан каталог.
 dpkg --forget-old-unavail 
Забыть о неустановленных недоступных пакетах.
 dpkg --clear-avail 
Очистить имеющуюся информацию о том, какие пакеты доступны.
 dpkg -C  |  --audit 
Найти пакеты, которые были установлены в системе только частично.  dpkg 
посоветует, что делать, чтобы восстановить их работоспособность.
 dpkg --get-selections  [ шаблон имени пакета ...] 
Get list of package selections, and write it to stdout. Without a pattern,
packages marked with state purge will not be shown.
 dpkg --set-selections 
Установить список выбранных пакетов, принимая его со стандартного
ввода. Этот файл должен быть в формате '<пакет>
<состояние>', где состоянием может быть install, hold, deinstall или
purge. Допускаются пустые строки и строки комментариев (начинающиеся с #).
 dpkg --clear-selections 
Set the requested state of every non-essential package to deinstall.  This
is intended to be used immediately before --set-selections, to deinstall any
packages not in list given to --set-selections.
 dpkg --yet-to-unpack 
Найти пакеты, выбранные для установки, но по какой-то причине ещё не
установленные.
 dpkg --print-architecture 
 
Print architecture of packages  dpkg  installs (for example, "i386").
 dpkg --compare-versions  ver1 op ver2 
Сравнить номера версий, используя  op  в качестве бинарного
оператора.  dpkg  возвращает код успешного завершения (нуль), если
указанное условие удовлетворяется, или неудачу (не-нуль) в противном
случае. Есть две группы операторов, которые работают по-разному при
отсутствующем параметре  ver1  или  ver2 . Операторы  lt le eq ne ge gt 
считают отсутствующий номер версии более ранним, чем любой другой. Операторы
 lt-nl le-nl ge-nl gt-nl  считают отсутствующий номер версии более поздним,
чем любой другой. Операторы  < << <= = >= >>
>  используются только для совместимости с синтаксисом файла control.
 dpkg --command-fd <n> 
Принимает серию команд из входного файлового дескриптора
 <n> . Замечание: дополнительные параметры, заданные в командной
строке, а также через этот дескриптор, не сбрасываются для последующих
команд, выполняющихся далее в этом запуске.
 dpkg --help 
Вывести небольшую подсказку.
 dpkg --force-help 
Вывести подсказку о параметрах  --force- что-нибудь .
 dpkg -Dh  |  --debug=help 
Вывести подсказку о параметрах отладки.
 dpkg --licence  |  dpkg --license 
Показать лицензию  dpkg .
 dpkg --version 
Показать информацию о версии  dpkg .
 dpkg-deb actions 
Смотрите  dpkg-deb (1) для более подробной информации о нижеследующих
действиях.
 
 dpkg -b  |  --build   каталог  [ имя файла ]
    Собирает deb пакет.
 dpkg -c  |  --contents   имя файла 
    Показывает содержимое deb пакета.
 dpkg -e  |  --control   имя файла  [ каталог ]
    Извлекает управляющую информацию из пакета.
 dpkg -x  |  --extract   имя файла  <каталог>
    Извлекает файлы, содержащиеся в данном пакете.
 dpkg -f  |  --field    имя файла  [ управляющее-поле ] ...
    Показывает управляющее(ие) поле(я) пакета.
 dpkg --fsys-tarfile   имя файла 
    Выдает на стандартный вывод tar-файл с файловой
