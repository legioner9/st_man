    Ищет в установленных пакетах имя файла по заданному шаблону.
 dpkg -p  |  --print-avail  I <имя пакета>
    Выводит детальную информацию о  пакете , которую извлекает
    из файла  /var/lib/dpkg/available .
 

 
   
 ПАРАМЕТРЫ 

Все параметры можно указывать как в командной строке, так и в
конфигурационном файле  dpkg  --  /etc/dpkg/dpkg.cfg . Каждая строка в
конфигурационном файле должна быть либо параметром (в точности как в
командной строке, но без начальных дефисов), либо комментарием (если
начинается с  # ).
 

 
 --abort-after= число 
Change after how many errors  dpkg  will abort. The default is 50.
 -B | --auto-deconfigure 
При удалении пакета есть вероятность, что есть другой установленный пакет
зависящий от удаляемого пакета. Указание этого параметра приведёт к
автоматической деконфигурации пакета, который зависит от удаляемого пакета.
 -D octal  | --debug= octal 
Switch debugging on.  octal  is formed by bitwise-orring desired values
together from the list below (note that these values may change in future
releases).  -Dh  or  --debug=help  display these debugging values.
 
Маска   Описание
     1   Основная полезная информация по ходу выполнения
     2   Вызов и состояния скриптов поддержки
    10   Вывод по каждому обработанному файлу
   100   Подробный вывод по каждому обработанному файлу
    20   Вывод по каждому конфигурационному файлу
   200   Подробный вывод по каждому конфигурационному файлу
    40   Зависимости и конфликты
   400   Подробное освещение зависимостей и конфликтов
  1000   Подробная ерунда, например о каталоге dpkg/info
  2000   Невероятное количество ерунды
 --force- things  |  --no-force- things  |  --refuse- things 
 
Принудительно выполнять или же отказываться выполнять ( no-force  и
 --refuse  означают одно и то же) те или иные вещи.  things  -- это список
через запятую элементов описанных далее).  --force-help  выдает сообщение,
описывающие их. Элементы, обозначенные звёздочкой (*), принудительно
выполняются по умолчанию.
 
 Предупреждение: Эти параметры предназначены только для использования
экспертами. Использование без полного понимания их действия может нарушить
работоспособность всей системы. 
 
 all : Turns on (or off) all force options.
 
 downgrade (*): Установить пакет, даже если уже установлена его более новая
версия.
 
 Предупреждение: На данный момент dpkg не выполняет проверку зависимостей
при переходе на более старую версию и поэтому не предупредит если это
поломает зависимости с остальными пакетами. Это может иметь серьёзные
последствия: замена на более старую версию системных компонентов первой
необходимости может привести к полной неработоспособности
системы. Использовать осторожно. 
 
 configure-any : Настроить также все распакованные, но ненастроенные
пакеты, от которых зависит текущий пакет.
 
 hold : Обрабатывать даже пакеты, помеченные как "зафиксированные".
 
 remove-reinstreq : Удалить пакет, даже если он сломан и отмечен как
требующий переустановки. Это может, например, привести к тому, что части
пакета останутся в системе, а  dpkg  про них забудет.
 
 remove-essential : Удалить, даже если это пакет первой необходимости
(имеет приоритет "Essential"). Эти пакеты в основном содержат базовые
команды Unix. Удаление такого пакета может привести к потере
работоспособности системы, так что используйте данный параметр осторожно.
 
 depends : Превратить проблемы с зависимостями в предупреждения.
 
 depends-version : Не обращать внимание на версии при проверке
зависимостей.
 
 conflicts : Установить, даже если пакет конфликтует с другим пакетом. Это
опасно, так как обычно приводит к перезаписи некоторых файлов.
 
 confmiss : Всегда устанавливать пропущенный конфигурационный файл. Это
опасно, так как не будут сохранены изменения (удаление) файла.
 
 confnew : Если конфигурационный файл был изменён, то всегда устанавливать
новую версию без запроса подтверждения, если не задан также параметр
 --force-confdef , при котором предпочтение отдаётся действию по умолчанию.
 
 confold : Если конфигурационный файл был изменён, всегда сохранять старую
версию без запроса подтверждения, если не задан также параметр
 --force-confdef , при котором предпочтение отдаётся действию по умолчанию.
 
 confdef : Если конфигурационный файл был изменён, всегда выбирать действие
по умолчанию. Если такового нет, то спросить у пользователя, если не задан
параметр  --force-confnew  или  --force-confold  (в этом случае
соответствующий параметр используется для выяснения, что именно нужно
делать).
 
 overwrite : Перезаписать файл из одного пакета файлом из другого пакета.
 
 overwrite-dir : Перезаписать каталог из одного пакета файлом из другого.
 
 overwrite-diverted : Перезаписать файл с изменённым
расположением(diverted) его неизменённой версией.
 
 architecture : Обрабатывать даже пакеты с неправильной архитектурой.
 
 bad-path : В  PATH  отсутствуют важные программы, поэтому могут
возникнуть проблемы.
 
 not-root : Попытаться установить или удалить пакет, даже если не нет прав
суперпользователя.
 
 bad-verify : Устанавливать пакет, даже если он не прошёл проверку
подлинности.
 
 --ignore-depends = пакет ,... 
Игнорировать проверку зависимостей для указанных пакетов (на самом деле,
проверка выполняется, но выдаются только предупреждения о конфликтах и
ничего более).
 --new  |  --old 
Выбрать, соответственно, новый или старый формат пакета. Это параметр
программы  dpkg-deb (1).
 --nocheck 
Не читать и не проверять содержимое файла control при создании пакета. Это
параметр программы  dpkg-deb (1).
 --no-act  |  --dry-run  |  --simulate 
Делать всё, что необходимо, но не записывать на диск изменения. Он
используется для того, чтобы посмотреть, что произошло бы при выполнении
указанного действия, но при этом ничего не меняется.
 
Убедитесь, что указали  --no-act  перед действием, или у вас могут
получиться нежелаемые результаты. (например,  dpkg --purge foo --no-act 
сперва выполнит полное удаление пакета foo, а затем будет пытаться полностью
удалить пакет --no-act, хотя вы ожидали, что ничего выполнено не будет)
 -R  |  --recursive 
Рекурсивная обработать все файлы, отвечающие шаблону  *.deb , которые
найдены в заданных каталогах и во всех их подкаталогах. Этот параметр можно
использовать вместе с действиями  -i ,  -A ,  --install ,  --unpack  и
 --avail .
 -G 
Don't install a package if a newer version of the same package is already
installed. This is an alias of  --refuse-downgrade .
 --root= dir  |  --admindir= dir  |  --instdir= dir 
Change default directories.  admindir  defaults to  /var/lib/dpkg  and
contains many files that give information about status of installed or
uninstalled packages, etc.  instdir  defaults to  /  and refers to the
directory where packages are to be installed.  instdir  is also the
directory passed to  chroot (2) before running package's installation
scripts, which means that the scripts see  instdir  as a root directory. 
Changing  root  changes  instdir  to  dir  and  admindir  to
 dir /var/lib/dpkg .
 -O  |  --selected-only 
Обрабатывать только пакеты, выбранные для установки. Такой выбор делается
программой  dselect  или же самой программой  dpkg  при работе с
пакетами. Например, когда пакет удаляется, он будет помечен для удаления.
 -E  |  --skip-same-version 
Не устанавливать пакет, если та же версия этого пакета уже установлена.
 --status-fd  <n> 
Send package status info to file descriptor  <n> . This can be
given multiple times. Status updates are of the form `status: <pkg>:
<pkg state>'.  Errors are reported as `status: <pkg>: error:
extend-error-message'.  Configuration file conflicts are reported as
`status: conffile-prompt: conffile : 'current-conffile' 'new-conffile'
useredited distedited'.
 --log= имя файла 
Log status change updates and actions to  filename , instead of the default
 /var/log/dpkg.log . If this option is given multiple times, the last
filename is used. Log messages are of the form `YYYY-MM-DD HH:MM:SS status
<state> <pkg> <installed-version>' for status change
updates; `YYYY-MM-DD HH:MM:SS <action> <pkg>
<installed-version> <available-version>' for actions where
 <action>  is one of install, upgrade, remove, purge; and
`YYYY-MM-DD HH:MM:SS conffile <filename> <decision>' for
conffile changes where  <decision>  is either install or keep.
 --no-debsig 
Не пытаться проверять подпись пакета.
 
   
 ФАЙЛЫ 

 
 /etc/dpkg/dpkg.cfg 
Файл конфигурации с параметрами по умолчанию.
 /var/log/dpkg.log 
Файл журнала по умолчанию (смотрите  /etc/dpkg/ dpkg.cfg (5) и параметр
 --log ).

Остальные файлы перечислены с указанием их каталогов по умолчанию, смотрите
параметр  admindir , чтобы узнать, как изменить местоположение этих файлов.
 /var/lib/dpkg/available 
Список доступных пакетов.
 /var/lib/dpkg/status 
Состояния доступных пакетов. Этот файл содержит информацию о том, помечен ли
пакет для удаления или нет, установлен он или нет, и т.д. Смотрите раздел
 ИНФОРМАЦИЯ О ПАКЕТАХ , где приведена подробная информация.

Нижеследующие файлы являются компонентами бинарного пакета. Смотрите
 deb (5), где приведена подробная информация:
 control 
 conffiles 
 preinst 
 postinst 
 prerm 
 postrm 
 
   
 ПЕРЕМЕННЫЕ ОКРУЖЕНИЯ 

 
 DPKG_NO_TSTP 
Если она определена, то  dpkg  при запуске оболочки не приостанавливает
себя, а создаёт новый процесс.
 SHELL 
The program  dpkg  will execute when starting a new shell.
 COLUMNS 
Задаёт количество колонок текста, используемых программой  dpkg  при
форматировании текста. В настоящий момент используется только с параметром
 -l .
 
   
 ПРИМЕРЫ 

To list packages related to the editor  vi (1):
 

       dpkg -l aq*vi*aq 
 

 
To see the entries in  /var/lib/dpkg/available  of two packages:
 

       dpkg --print-avail elvis vim | less 
 

 
Вручную просмотреть список пакетов:
 

       less /var/lib/dpkg/available 
 

 
Удалить установленный пакет elvis:
 

       dpkg -r elvis 
 

 
Чтобы установить пакет, сначала нужно найти его в архиве или на CDROM. Файл
"available" показывает, что пакет vim находится в секции "editors"
("редакторы"):
 

       cd /cdrom/hamm/hamm/binary/editors 
       dpkg -i vim_4.5-3.deb 
 

 
Получить локальную копию состояний выбранных пакетов:
 

       dpkg --get-selections >myselections 
 

 
Вы можете перенести этот файл на другой компьютер и установить его там с
помощью:
 

       dpkg --clear-selections 
       dpkg --set-selections <myselections 
 

 
Заметим, что при этом ничего не будет ни установлено, ни удалено, просто
будут заданы состояния запрошенных пакетов. Для того, чтобы на самом деле
скачать и установить запрошенные пакеты, вам понадобится другая
программа. Например, запустите  dselect  и выберите "Установить".
 
Обычно  dselect (8) предоставляет более удобный способ изменить состояния
выбранных пакетов.
 

   
