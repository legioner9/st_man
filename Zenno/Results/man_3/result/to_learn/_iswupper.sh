 СМ. ТАКЖЕ 

 isupper (3),

 iswctype (3),

 towupper (3)

   
 ЗАМЕЧАНИЯ 

Поведение  iswupper  зависит от категории LC_CTYPE текущей локали.
 

Данная функция не совсем подходит для работы с символами Unicode,
т.к. Unicode оперирует тремя регистрами: верхним, нижним и заглавным.
 

 
