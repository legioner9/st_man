 НАЗВАНИЕ 

iswupper - проверяет, является ли символ широким символом верхнего регистра
   
 СИНТАКСИС 

 #include < wctype.h > 

 int iswupper(wint_t  wc ); 
 

   
 ОПИСАНИЕ 

Функция  iswupper  является эквивалентом функции  isupper  для работы
с широкими символами. Она проверяет, является ли  wc  широким символом,
принадлежащим классу "upper".
 

Класс широких символов "upper" является подклассом класса широких символов
"alpha" и, следовательно, подклассом таких классов широких символов, как:
"alnum", "graph" и "print".
 

Будучи подклассом класса широких символов "print", "upper"
отделен от класса широких символов "cntrl".
 

Будучи подклассом класса широких символов "graph", "upper"
отделен от класса широких символов "space" и его подкласса "blank".
 

Будучи подклассом класса широких символов "alnum", "upper"
отделен от класса широких символов "punct".
 

Будучи подклассом класса широких символов "alpha", "upper"
отделен от класса широких символов "digit".
 

Класс широких символов "upper" всегда содержит,
по меньшей мере, такие символы  wc , которые равны  towupper(wc) 
и отличаются от  towlower(wc) .
 

Класс широких символов "upper" всегда содержит, по меньшей мере,
буквы от 'A' до 'Z'.
   
 ВОЗВРАЩАЕМЫЕ ЗНАЧЕНИЯ 

Функция  iswupper  возвращает ненулевое значение, если
 wc  является широким символом, принадлежащим классу
широких символов "upper". В других случаях возвращается ноль.
   
