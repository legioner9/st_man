 НАЗВАНИЕ 

tgamma, tgammaf, tgammal - вычисление настоящей гамма-функции
   
 СИНТАКСИС 

 #include < math.h > 

 
 double tgamma(double  x ); 

 

 float tgammaf(float  x ); 

 

 long double tgammal(long double  x ); 

 
   
 ОПИСАНИЕ 

Эта функция возвращает значение гамма-функции для аргумента  x .
Она называется "настоящей гамма-функцией"("true gamma function")
так как уже есть функция 
 gamma() 

возвращающая совершенно другое значение.
Смотрите
 lgamma (3).

   
