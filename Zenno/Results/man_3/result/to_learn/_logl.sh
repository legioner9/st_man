 НАЗВАНИЕ 

log - функция натурального логарифма
   
 СИНТАКСИС 

 #include < math.h > 

 double log(double  x ); 

 float logf(float  x ); 

 long double logl(long double  x ); 
 

   
 ОПИСАНИЕ 

Функция  log()  возвращает значение натурального логарифма для  x .
   
 НАЙДЕННЫЕ ОШИБКИ 

Функция  log()  может возвратить такие ошибки:
 
 EDOM 

 
Аргументом  x  является отрицательное число.
 ERANGE 

 
Аргументом  x  является ноль. Логарифм для нуля неопределен
(вообще, он равен минус бесконечности).
 
   
 СООТВЕТСТВИЕ СТАНДАРТАМ 

SVID 3, POSIX, BSD 4.3, ISO 9899.
Варианты функции для чисел с плавающей точкой и для длинных двойных чисел
являются обязательными требованиями стандарта C99.
   
