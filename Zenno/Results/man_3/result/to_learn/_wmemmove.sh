 НАЗВАНИЕ 

wmemmove - копирование массива широких символов
   
 СИНТАКСИС 

 #include < wchar.h > 

 wchar_t *wmemmove(wchar_t * dest , const wchar_t * src , size_t  n ); 
 

   
 ОПИСАНИЕ 

Функция  wmemmove  является эквивалентом функции  memmove   для
работы с широкими символами. Она копирует  n  широких символов из
массива  src  в массив  dest . Массивы могут частично
перекрываться.
 

Программист должен убедиться, что есть достаточно места хотя бы для
 n  широких символов в массиве  dest .
   
 ВОЗВРАЩАЕМЫЕ ЗНАЧЕНИЯ 

 wmemmove  возвращает  dest .
   
