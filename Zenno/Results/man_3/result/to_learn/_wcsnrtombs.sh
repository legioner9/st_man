 ЗАМЕЧАНИЯ 

Поведение функции 
 wcsnrtombs 

зависит от категории LC_CTYPE текущей локали.
 

Передавать NULL в качестве
 ps 

небезопасно с точки зрения работы подпроцессов.
 

 
