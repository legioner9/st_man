 НАЗВАНИЕ 

ausearch_clear - очистка параметров поиска
   
 СИНТАКСИС 

 #include < auparse.h > 

 
void ausearch_clear(auparse_state_t *au);
 
   
 ОПИСАНИЕ 

 
ausearch_clear очищает все параметры поиска, хранящиеся в экземпляре парсера, и освобождает связанную с ними память.
 
   
 ВОЗВРАЩАЕМОЕ ЗНАЧЕНИЕ 

 
Функция не возвращает значение.
 
   
