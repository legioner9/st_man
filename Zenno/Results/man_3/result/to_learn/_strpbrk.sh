 НАЗВАНИЕ 

strpbrk - производит поиск строки для любого набора символов
   
 СИНТАКСИС 

 #include < string.h > 

 char *strpbrk(const char * s , const char * accept ); 
 

   
 ОПИСАНИЕ 

Функция  strpbrk()  ищет первое совпадение в строке  s   
с любым символом из строки  accept .
   
 ВОЗВРАЩАЕМЫЕ ЗНАЧЕНИЯ 

Функция  strpbrk()  возвращает указатель на символ в  s ,
соответствующий одному из символов в наборе  accept , или NULL,
если такого символа нет в строке.
   
