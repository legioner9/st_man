 НАЗВАНИЕ 

auparse_timestamp_compare - сравнение штампов времени
   
 СИНТАКСИС 

 #include < auparse.h > 

 
int auparse_timestamp_compare(au_event_t *e1, au_event_t *e2);
 
   
 ОПИСАНИЕ 

 
auparse_timestamp_compare сравнивает два штампа времени.
 
   
 ВОЗВРАЩАЕМОЕ ЗНАЧЕНИЕ 

 
Возвращает -1, 0, или 1 в зависимости от того, будет e2 меньше, равно или больше e1 соответственно.
 
   
