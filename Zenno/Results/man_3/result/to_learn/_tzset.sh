 ЗАМЕЧАНИЯ 

Обратите внимание, что переменная  dailight  не показывает,
используется ли "летнее" время прямо сейчас. Она используется для
указания номера алгоритма (см. переменную  tz_dsttime  в
 gettimeofday (2)).

Многие годы она считается устаревшей, но по-прежнему включена в SUSv2.
 

В BSD4.3 имелась функция  char *timezone(zone,dst) , которая
возвращала имя временной зоны, соответствующей первому аргументу
(минуты западнее GMT). Если второй аргумент не равнялся 0, то
использовалось стандартное имя, иначе использовалось версия "летнего"
времени.
   
