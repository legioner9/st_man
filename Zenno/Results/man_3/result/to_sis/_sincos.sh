 НАЗВАНИЕ 

sincos - вычисляет одновременно синус и косинус
   
 СИНТАКСИС 

 #include < math.h > 

 double sincos(double  x , double * sin , double * cos ); 

 float sincosf(float  x , float * sin , float * cos ); 

 long double sincosl(long double  x , long double * sin , long double * cos ); 
 

   
 ОПИСАНИЕ 

Несколько приложений требуют вычисление синуса и косинуса угла
 x .

Эта функция вычисляет их одновременно, и сохраняет результаты
через заданные указатели.
   
