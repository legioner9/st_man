 НАЙДЕННЫЕ ОШИБКИ 

В Linux, все генерируемые сигналы являются уникальными и на каждый из них
система отвечает уникальным процессом. При чрезвычайной перегруженности ситемы
 ITIMER_REAL 

может закончиться до того момента, как будет доставлен сигнал от предыдущего.
И таким образом, сигнал от последующего таймера будет потерян.
 

 
