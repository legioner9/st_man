 set_thread_area (2) >>  set_thread_area  (2)   ( Русские man: Системные вызовы )   set_thread_area  (2)   ( Linux man: Системные вызовы ) 
   
 НАЗВАНИЕ 

set_thread_area - установка области TLS
   
 СИНТАКСИС 



 
#include < linux/unistd.h >
 
 
#include < asm/ldt.h >
 
 
 
 int  set_thread_area  (struct user_desc  *u_info );


 
 
   
 ОПИСАНИЕ 

 
 

 set_thread_area  устанавливает элемент в текущем массиве подпроцесса
хранилища локальных подпроцессов (TLS, Thread Local Storage).
Индекс записи элемента массива TLS, устанавливаемого  set_thread_area ,
соответствует значению  u_info-> entry_number , переданного от
пользователя. Если это значение находится в допустимымх пределах, то
 set_thread_area  копирует соответствующий описатель, на который
указывает  u_info , в массив TLS подпроцессов.
 
 

Когда  set_thread_area  передает  entry_number , равный -1, 
то используется свободная область TLS. 
Если  set_thread_area  находит свободную область TLS, то значение
 u_info-> entry_number  устанавливается после возврата
для показа того, какая же область была изменена.
 
   
 ВОЗВРАЩАЕМЫЕ ЗНАЧЕНИЯ 

 
 

 set_thread_area  возвращает 0 при нормальном завершении работы.
Иначе возвращается одна из ошибок, указанных в разделе "Ошибки".
 
   
 НАЙДЕННЫЕ ОШИБКИ 

 
 
 EINVAL 
 u_info-> entry_number  вне допустимых границ.
 
 EFAULT 
 u_info  является некорректным указателем.
 
 ESRCH 
Невозможной найти свободную область TLS.
 
 
   
 СООТВЕТСТВИЕ СТАНДАРТАМ 

 
 

 set_thread_area  специфично для Linux
и не должно использоваться в портируемых программах.
 
   
 ВЕРСИИ 

 
 

Версия  set_thread_area  в первый раз появилась в Linux 2.5 в августе 2002.
 
   
