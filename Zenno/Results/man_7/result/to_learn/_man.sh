 БЕЗОПАСНОСТЬ 

Несмотря на то, что технически
 man 

- это макро-пакет, основанный на troff, на самом деле существует
большое количество других утилит, работающих с файлами man-страниц,
которые не включают в себя всех возможностей troff.
Таким образом, лучше избегать всех экзотических возможностей troff
везде, где это возможно.
Избегайте использования различных препроцессоров troff
(если необходимо, используйте
 tbl (1),

но старайтесь использовать команды
 IP 

и
 TP 

для двухколоночных таблиц).
Избегайте использования вычислений; многие утилиты не обрабатывают
их. Используйте простые команды, которые легко преобразовывать в другие форматы.
Следующие макросы troff считаются безопасными (во многих
случаях они игнорируются преобразователями форматов):
 \ , 

 . ,

 ad ,

 bp ,

 br ,

 ce ,

 de ,

 ds ,

 el ,

 ie ,

 if ,

 fi ,

 ft ,

 hy ,

 ig ,

 in ,

 na ,

 ne ,

 nf ,

 nh ,

 ps ,

 so ,

 sp ,

 ti ,

 tr .

 

Вы можете использовать и многие escape-последовательности troff,
начинающиеся с \).
Если Вы хотите проставить обратный слэш в тексте, то используйте \e.
Вы можете использоть и другие последовательности, где x или xx -
это любые символы, а N - это цифра, в том числе:
 \' ,

 \` ,

 \- ,

 \. ,

 \ , 

 \% ,

 \*x ,

 \*(xx ,

 \(xx ,

 \$N ,

 \nx ,

 \n(xx ,

 \fx 

и
 \f(xx .

Избегайте использования экранирующих последовательностей для вывода графики.
 

Не используйте необязательный параметр команды
 bp 

(разрыв страницы).
Используйте только положительные значения для задания
 sp 

(вертикального сдвига).
Не определяйте макрос
( de )

с тем же именем в данном пакете или пакете mdoc,
в котором этот макрос имеет другое значение;
скорее всего, такие определения будут
проигнорировани.
Каждый положительный отступ
( in )

должен сочетаться с соответствующим отрицательным
отступом (хотя лучше использовать вместо этого
макросы
 RS 

и
 RE 

).
Условный оператор
( if,ie )

в качестве условий должен иметь только 't' или 'n'.
Из преобразований
( tr )

должны использоваться только те, которые могут быть проигнорированы.
Изменения шрифтов
( ft 

и экранирующие последовательности  \f )
должны использоваться только со значениями 1, 2, 3, 4, R, I, B, P или CW
(у команды ft может и не быть параметров).
 

Если Вы используете другие возможности troff, 
то аккуратно проверьте результаты на нескольких утилитах.
После того, как Вы убедитесь в безопасности этой команды,
дайте знать об этом администратору данного документа,
чтобы он мог включить безопасную команду или экранирующую последовательность в этот список.
   
 ЗАМЕЧАНИЯ 

 

Любыми способами включайте полные URL (или URI) в сам текст;
многие утилиты, такие, как
 man2html (1),

могут автоматически преобразовывать их в ссылки.
Вы также можете использовать новый макрос
 UR 

для создания ссылок на сопутствующую информацию.
Если Вы используете URL, то используйте полную форму
(например, < http://www.kernelnotes.org >) для того, чтобы утилиты
могли их автоматически найти.
 

Утилиты, обрабатывающие подобные файлы, должны открыть файл
и рассмотреть первый непустой символ.  Точка (.) или апостроф (')
в начале файла означают файл в формате troff (man или mdoc).
Левая угловая скобка (<) означает файл в формате SGML/XMSL
(например, HTML или Docbook). Все остальное должно считаться
простым ASCII-текстом (например, результатом работы "catman").
 

Многие страницы начинаются с символов "\" , за которыми
следуют пробелы и список символов, определяющих, как должна
обрабатываться страница.
Для совместимости с не-troff конверторами мы рекомендуем избегать
использования препроцессоров, кроме
 tbl (1),

который определяется Linux автоматически.
Однако, Вы можете поместить в данный документ сходную информацию для того, чтобы
Ваша страница могла обрабатываться другими системами (с меньшими
возможностями).
Ниже определены типы препроцессоров, запускаемые этими символами:
 
 e 

 
 eqn (1)
 g 

 
 grap (1)
 p 

 
 pic (1)
 r 

 
 refer (1)
 t 

 
 tbl (1)
 v 

 
 vgrind (1)
 
   
