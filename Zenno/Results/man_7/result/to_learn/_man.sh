 ОПИСАНИЕ 

На этой странице описывается макропакет
 groff tmac.an 

(часто называемый макропакетом
 man )

и соглашения по созданию страниц руководства (manual pages).
Этот макропакет должен использоваться разработчиками при
написании страниц руководства по Linux или переносе их с других систем.
Вариант Linux практически полностью совместим с другими версиями этого
макропакета, поэтому с переносом не должно возникнуть проблем (за исключением
NET-2 BSD, использующего совсем другой пакет под названием mdoc; смотрите
 mdoc (7)).

 

Заметьте, что mdoc-страницы руководства по NET-2 BSD могут быть
просмотрены при помощи
 groff 

путем указания опции
 -mdoc 

вместо 
 -man .

Однако, рекомендуется использование
 -mandoc ,

так как в этом случае формат будет распознан автоматически.
   
 ПРЕАМБУЛА 

Первой на странице руководства (после комментариев) должна быть команда
 
 
 .TH 

 заголовок раздел дата источник документация ,

 
 

где:
 
 
 заголовок 

 
Заголовок страницы руководства (например,
 MAN ).

 раздел 

 
Номер раздела, где должна находиться страница руководства (например,
 7 ).

 дата 

 
Дата последнего изменения. Hе забывайте изменять эту дату
каждый раз, так как это наиболее простой способ контролировать версии
документа.
 source 

 
Источник.
 
Для задания команд надо использовать что-то вроде:
 GNU ,  NET-2 ,  SLS Distribution ,  ASPLinux Distribution .

 
Для системных вызовов используйте версию ядра, с которой Вы работаете, например:
 Linux 0.99.11 .

 
Для библиотечных вызовов используйте один из источников:
 GNU ,  BSD 4.3 ,  Linux DLL 4.4.1 .

 документация 

 
Название руководства (например,
 Linux Programmer's Manual ).

 
 

 

Заметьте, что mdoc-страницы в BSD начинаются с команды
 Dd ,

а не с
 TH .

 

