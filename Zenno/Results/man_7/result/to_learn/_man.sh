 НАЙДЕННЫЕ ОШИБКИ 

 

Многие макросы описывают форматирование (например, тип шрифта и расстояния) вместо
разметки семантики содержания (например, этот текст является ссылкой на другую
страницу) в отличие от других форматов типа mdoc и DocBook
(даже в HTML больше семантических меток).
По этой причине сложнее преобразовывать формат
 man 

в другой формат, сохраняя информационную насыщенность нового формата
и автоматическую вставку ссылок.
Если Вы будете задавать безопасные команды, описанные выше,
то в будущем автоматически перейти в другой формат Вам будет гораздо легче.
 

