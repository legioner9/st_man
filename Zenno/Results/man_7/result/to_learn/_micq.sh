недействительных для данной кодировки.
 colorerror  цвет глобальный 

 
Цвет, используемый для ошибок.
 colordebug  цвет глобальный 

 
Цвет отладочного вывода.
 colorquote  цвет глобальный 

 
Цвет цитированных строк.
 webaware  логический соединение 

 
Должен ли текуший статус быть виден через сайт.
 hideip  логический соединение 

 
Скрывать ли свой адрес IP.
 dcauth  логический соединение 

 
Могут ли авторизованные контакты видеть адрес IP.
 dccont  логический соединение 

 
Могут ли контакты видеть адрес IP.
 obeysbl  логический/ соединение 

 
Загружать ли список контактов с сервера при входе. В этом режиме
авторизации будут нужны, тогда как для локальных списков контактов
они игнорируются.
 ignore  логический/ контакт 

 
Игнорировать ли все сообщения от этого контакта.
 hidefrom  логический/ контакт 

 
Всегда невидимый для этого контакта.
 intimate  логический/ контакт 

 
Всегда видимый для этого контакта.
 logonoff  логический/ контакт 

 
Протоколировать подключение и отключение этого контакта.
 logchange  логический/ контакт 

 
Протоколировать изменения статуса контакта.
 logmess  логический/ контакт 

 
Писать в журнал сообщения этого контакта.
 showonoff  логический/ контакт 

 
Показывать подключение и отключение этого контакта.
 showchange  логический/ контакт 

 
Показывать изменения статуса этого контакта.
 hideack  логический/ контакт 

 
Скрывать подтверждения сообщений к этому контакту.
 

