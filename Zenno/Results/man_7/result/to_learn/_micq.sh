в строку;
 
 

Новые параметры в 0.4.10. Параметры hermit, log, logonoff, silent, tabs удалены в 0.5;
для их изменения используйте команду
 opt . 

 

 opt  [<контакт>|<группа>|соединение|global [<параметр> [<значение>]]] 

 
Задать
 значение параметра 

для
 контакта , группы контактов ,

текущего соединения или глобально, либо показать текущее значение, либо
показать все параметры и их значения.
 

Когда параметры применяются к контактам, в первую очередь применяется параметр,
если он задан, для самого контакта, во вторую - параметр для группы, к которой
относится контакт, в третью - для серверного соединения, если контакт доступен
по нему, в последнюю очередь - глобальный параметр. Если параметр не задан
вообще, для числовых параметров используется 0, для логических - FALSE (ложно),
для цветовых строка "no color" (без цвета), а для строчных - пустая строка.
 

Параметры для групп применяются аналогично, за вычетом первого шага, для
соединений за вычетом второго, а глобальные - только глобально.
 

На текущий момент есть следующие параметры, по типу и применению:
 
 
 colorscheme  число глобальный 

 
Номер используемой цветовой схемы. Чтобы запретить либо использовать
цвета, заданные ниже, поставьте 0.
 colornone  цвет глобальный 

 
Цвет, используемый для обычного текста. Ключевые слова
 black ,

 red ,

 green ,

 yellow ,

 blue ,

 magenta ,

 cyan ,

 white ,

 none ,

и
 bold 

понмаются и заменяются на соответствующие управляющие
последовательности ANSI. Учтите, что любой цвет уже включает в себя
 none ,

так что указывайте
 bold 

всегда после цвета. Любой другой текст считается управляющими символами,
которые выводятся как есть.
 colorserver  цвет глобальный 

 
Цвет, используемый для сообщений сервера.
 colorclient  цвет глобальный 

 
Цвет, используемый для сообщений клиентов.
 colorinvchar  цвет глобальный 

 
