 ^ .

 
 & 

 
С этим контактом установлено прямое соединение.
 ba 

 
Прямое соединение с этим контактом не удалось.
 : 

 
Производится попытка установить прямое соединение.
 ^ 

 
Прямого соединения нет, но адрес IP и номер порта известны.
 
 

Для групп команд
 ww* 

и
 ee* 

будет показан ещё один столбец, в котором выводится статус на основе
списка контактов на сервере, насколько он известен:
 
 S 

 
Контакт есть в списке контактов на сервере и должен там быть.
 s 

 
Контакт есть в списке на сервере, но его там не должно было быть.
 . 

 
Контакта нет в списке на сервере, но он должен бы там быть. Обычно это
означает "требуется авторизация".
 ' ' 

 
Контакт отсутствует в списке на сервере, и его не хотят там видеть.
 
 

