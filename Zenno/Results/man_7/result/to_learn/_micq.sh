mICQ - интерактивные команды
   
 ОПИСАНИЕ 

Эта справочная страница описывает команды, которые могут быть использованы
в mICQ. Информация, которая содержится здесь, совпадает с интерактивной
справкой, но более точна и подробна.
Правила нотации: квадратные скобки
( [] )

означают необязательные параметры, тогда как угловые скобки
( <> )

обозначают необходимые параметры (и отделяют их от ключевых слов).
В частности,
 <uin> 

означает обязательный параметр UIN,
тогда как
 <контакты> 

обозначает разделяемый запятыми список контактов, то есть, UINов и
имён. Если этот аргумент последний, контакты также могут разделяться
пробелами.
   
 КОМАНДЫ, ОТНОСЯЩИЕСЯ К ОТПРАВКЕ СООБЩЕНИЙ 

 
 msg  <контактам> [<сообщение>] 

 
Отправить всем
 контактам .

Если указано
 сообщение ,

либо если
 сообщение 

заканчивается обратным наклоном
( rs ),

включается многострочный режим. Чтобы отправить многострочное сообщение,
нужно указать на пустой строке точку
( . ) . 

Отмена отправки осуществляется знаком решётки
( # )

на пустой строке.
 

Исходящие и входящие сообщения помечаются по-разному в зависимости от
использованного метода передачи.
 {{{ 

и
 }}} 

означают сообщения, отправленные по прямому соединению.
 {%{ 

и
 }%} 

означают сообщения, которые были отправлены по прямому соединению и
зашифрованы (требует наличия GnuTLS или OpenSSL на стадии сборки).
 {<< 

и
 >>} 

означают сообщения icq8 (называемые также Тип-2).
Наконец,
 <<< 

и
 >>> 

означают сообщения icq5, которые могут быть так называемые Тип-1,
Тип-4 или отложенные. Доставка этих типов сообщений не подтверждается.
 

При отправке сообщения сперва делается попытка использовать уже
существующее прямое соединение; если его ещё не установлено, оно
устанавливается в фоне для последующих сообщений. Если прямое
соединение не открыто, либо сообщение не подтверждается в течение
некоторого периода, соединение считается неуспешным, и предпринимается
попытка использовать следующий транспорт - отправка сообщения Тип-2.
Этот шаг будет пропущен, если та сторона не обозначила свою способность
принимать такие сообщения. Если этот шаг пропущен, не дал результата
либо вернул ошибку, например, контакт в это время отключился, сообщение
отправляется как обычное сообщение Тип-4.
 

Если та сторона обозначает свои возможности по приёму, сообщения
отправляются в кодировке UTF-8 и помечаются соответственно. В противном
случае сообщение отправляется в кодировке, обозначенной как стандартная
для контакта, либо, если не задана, в стандартной подразумеваемой
кодировке. Сообщения Тип-1, Тип-4 и отложенные не могут быть помечены
кодировкой, и поэтому передача любых символов, кроме латиницы, зависит
от правильной конфигурации и некоторой эвристики.
