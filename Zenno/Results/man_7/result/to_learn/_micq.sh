строку перевода номер
 N .

Чтобы выбрать язык, заданный переменными среды
 LANG ,

 LC_ALL 

и
 LC_MESSAGES ,

наберите
 ! ,

 auto 

или
 default .

Чтобы выключить перевод вообще, наберите
 . ,

 none 

или
 unload .

Чтобы увидеть номера строк перевода, предварите
код языка префиксом
 debug .

 
Файлы из ~/.micq/i18n/*.i18n предпочитаются глобальным, а
файлы из заданной базы BASE/i18n/*.i18b предпочитаются локальным
~/.micq/i18n/*.i18n, кроме того, "смешные" переводы предпочитаются
стандартным, если включён параметр
 funny .

 
Можно указать столько параметров, сколько захочется, например,
чтобы вывести строку 117 немецкого перевода и переключиться
обратно на обычный язык, наберите "trans de 117 default".
 uptime 

 
Показывает время, на протяжении которого mICQ работал, плюс
кое-какую статистику.
 set  <параметр> <значение> 

 
Установить
 параметр 

либо в
 on 

(вкл) или
 off ,

(выкл) либо одно из возможных
 значений . 

 Параметр  

может быть один из:
 
 
 color :

 
использовать цвета,
 delbs :

 
трактовать символы delete (стирания под курсором) как backspace
(стирание после курсора).
 funny :

 
использовать смешные сообщения,
 auto :

 
посылать автоответы, когда Вы отлучились,
 uinprompt :

 
использовать последнее из имён как строку приглашения,
 autosave :

 
автоматически записывать настройки в micqrc,
 autofinger :

 
автоматически запрашивать информацию о новых UINах,
 linebreak :

 
установить способ переноса сообщений как
 simple 

(простой) - печатать сообщение после имени, добавлять перенос,
 break 

(разрыв) - добавлять перевод строки перед выводом каждого
входящего сообщения,
 indent 

(отступ) - выводить все строки сообщения через отступ после имени, либо
 smart 

