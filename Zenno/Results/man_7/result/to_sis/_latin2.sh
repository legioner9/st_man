 НАЗВАНИЕ 

iso_8859-2 - набор символов, закодированных в восьмеричной,
десятичной и шестнадцатеричной системах исчисления согласно стандарту ISO 8859-1
   
 ОПИСАНИЕ 

Стандарт ISO 8859 включает в себя несколько 8-битных расширений для
набора символов ASCII (так же известного как ISO 646-IRV).  ISO 8859-2, "Latin Alphabet No. 2",
используется для кодирования символов центрально- и восточноевропейских языков 
и поэтому поддерживается некоторыми производителями программ.

Стандарт ISO 8859-2 поддерживает следующие языки: албанский, боснийский,
хорватский, чешский, английский, финский, немецкий, венгерский, ирландский, польский,
словацкий, словенский и сербский.

Заметьте, что некоторые языки на основе кириллицы имеют прямые транслитерации в Latin 2: 
македонский и сербский.

   
 Алфавиты ISO 8859 

Полный список алфавитов ISO 8859 включает в себя:

 
