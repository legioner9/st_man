 ISO 8859-3 Языки юго-восточной Европы и другие (Latin-3) 
 ISO 8859-4 Скандинавские/балтийские языки (Latin-4) 
 ISO 8859-5 Кириллицу (Latin/Cyrillic) 
 ISO 8859-6 Арабский (Latin/Arabic) 
 ISO 8859-7 Греческий (Latin/Greek) 
 ISO 8859-8 Иврит (Latin/Hebrew) 
 ISO 8859-9 Модификацию Latin-1 турецкого языка (Latin-5) 
 ISO 8859-10 Саамский/скандинавский/эскимосский (Latin-6) 
 ISO 8859-11 Тайский 
 ISO 8859-13 Прибалтийские языки (Latin-7) 
 ISO 8859-14 Кельтский (Latin-8) 
 ISO 8859-15 Западноевропейские языки (Latin-9) 
