 НАЗВАНИЕ 

ftphosts - файл доступа отдельных хостов к ftpd
 
   
 ОПИСАНИЕ 

Файл ftphosts используется для разрешения или запрещения
определенных аккаунтов с различных хостов.
 
 
 ВОЗМОЖНОСТИ ДОСТУПА 
 
allow <username> <addrglob> [<addrglob> ...] 
Позволяет хостам соответствующим <addrglob> входить
систему как пользователь <username>.
 
 deny <username> <addrglob> [<addrglob> ...] 
Всегда запрещает хостам соотвествующим <addrglob>
входить в систему как пользователь <username>.
 
 
   
