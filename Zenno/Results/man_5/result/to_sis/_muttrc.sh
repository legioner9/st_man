С операторами  ~m ,  ~n , и  ~z  вы также можете
определить диапазоны в формах  < MAX ,  > MIN ,
 MIN - , и  - MAX .
   
 Соответствие дат 

 

Операторы  ~d  и  ~r  используются для соответствия диапазонам дат,
интерпретируемым как указано в вашей локальной временной зоне.
 

Дата имеет форму
 DD [ / MM [ / [ cc ] YY ]], то есть дата с двумя цифрами, произвольно следующая за двузначным месяцем, произвольно следующая за определением года. Значение пропущенных полей, по умолчанию, текущий месяц и год.
 

Mutt понимает либо двух-, либо четырехзначное определение года. Если задан двузначный год, mutt интерпретирует значение меньше 70-и как начало 21го века, т.e., lq38rq означает 2038, а не 1938,
и lq00rq интерпретируется как 2000), а значение, большее или равно 70 как находящееся в 21ом веке.
 

Обратите внимание. Это поведение  схоже  с проблемой Y2K, но mutt
 не  имеет проблемы Y2K.
 

Если диапазон дат состоит из единственной даты, то разыскиваемый оператор будет в точности соответствовать этой дате. Если диапазон дат содержит тире (lq - rq), следующее перед датой, то этот диапазон будет соответствовать любой дате перед и до данной даты. Аналогично тире, стоящее после даты, будет соответствовать любой дате после указанной. Две даты, разделенные тире, соответствуют любой дате в заданный отрезок времени.
 

Вы так же можете указать точку смещения относительно текущей даты. Смещение определяется одним из символов
 < ,

 > ,

 = ,

сопровождаемых положительным числом, сопровождаемым одним из символов блока
 y ,

 m ,

 w , или

 d ,

определяя блок из лет, месяцев, недель или дней. Смещение, начинающееся со знака
 > 

соответствует датам, которые старше, чем указанное количество времени, 
смещение, начинающееся со знака
 <  

соответствует датам, которые моложе, чем указанное количество времени и 
смещение, начинающееся со знака
 = 

соответствует точно заданной точке во времени, некоторое количество времени назад.
   
