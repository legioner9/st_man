 НАЗВАНИЕ 

 muttrc  

- Файл конфигурации для Mutt Mail User Agent
   
 ОПИСАНИЕ 

 

Файл конфигурации mutt состоит из ряда lqкомандrq.
Каждая строка файла содержит одну, или более, команду. Если используются строки содержащие несколько команд подряд, то они должны быть разделены через точку с запятой (lq ; rq).
 

Знак диеза, или фунта, (lq # rq), используется как символ lqкомментарияrq. Вы можете использовать это для аннотаций в вашем файле инициализации. Любой текст после знака комментария до конца строки игнорируется.
 

Одинарные кавычки (lq ' rq) и двойные кавычки (lq " rq)
могут быть использованы для цитирования строк содержащих пробелы или другие специальные символы. Различие между двумя разными типами кавычек схоже с различием во многих популярных программах оболочки, а именно, одинарные кавычки используются для указания буквального цитирования (которое не интерпретируется для переменных оболочки или выделяется знаком обратного слэша [смотри следующий параграф]), тогда как двойные кавычки обозначают строку, которая подлежит интерпретации. Например, обратные тики заключены в двойные, а не одинарные кавычки.
 

 rs  служит знаком комментария для следующего за ним знака также, как в оболочках bash и zsh.
Например, если хотите поместить комментарий в теле цитаты (lq " rq), вы можете использовать lq rs rq для преобразования следующего символа в цитируемый вместо интерпретируемого.
 

lq rsrs rq означает вставку цитирования lq rs rq в строку. lq rsn rq и lq rsr rq означают обычные значения C, означающие перевод строки и возврат каретки, соответственно.
 

lq rs rq в конце строки может быть использован для разбивки команды на несколько строк при условии, что знак разбивки находится не в середине имени команды.
 

Так же возможно заменить вывод Unix-команды в файле инициализации. Это осуществляется путем заключения команды в обратные кавычки ( ` команда ` ).
 

К переменным UNIX можно обращаться способом, аналогичным реализованному в оболочках sh и bash, предваряя название переменной знаком доллара (lq Do rq).
   
