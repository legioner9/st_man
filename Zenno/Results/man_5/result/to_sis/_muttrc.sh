 ~T отмеченные сообщения 
 ~t  EXPR сообщения адресованные к  EXPR 
 ~U непрочитанные сообщения 
 ~v сообщение есть часть свернутой нити. 
 ~x  EXPR сообщения содержащие  EXPR  в поле lqReferencesrq  
 ~z  MIN - MAX сообщения с размером в диапазоне от  MIN  до  MAX 
