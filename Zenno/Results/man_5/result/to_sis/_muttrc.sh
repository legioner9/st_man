 МАСКИ 

 

В различных частях mutt, включая вышеупомянутые  hook-команды , вы можете определить маски соответствия для сообщений.
   
 Создание масок 

 

Простая маска состоит из оператора формы lq ~ символ rq, возможно сопровождаемого параметром с которым mutt предполагает соответствие объекта, определенного оператором. (Описание операторов смотри ниже.)
 

Разыскиваемый объект, соответствующий некоторым из этих операторов, может совпасть с несколькими e-mail адресами. В таком случае, совпавшим объектом, будет по крайней мере один из этих подходящих e-mail адресов. Вы можете добавить к маске символ (lq ^ rq) для обозначения, что  все  адреса должны проверяться на соответствие с разыскиваемым объектом.
 

Вы можете сконструировать комплекс масок комбинируя простые маски с логическими операторами. Логическое И определяется простым объединением двух простых масок, например: lq~C mutt-dev ~s bugrq. 
Логическое ИЛИ определяется вставкой вертикальной черты (lq | rq) между двумя масками, для примера: lq~C mutt-dev | ~s bugrq.
Дополнительно, вы можете реверсировать маску, указав знак восклицания (lq ! rq). Для логического объединения используйте скобки (lq()rq). Например: lq!(~t mutt|~c mutt) ~f elkinsrq.
   
