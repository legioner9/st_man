 ~d  MIN - MAX сообщения с lqдата-отправкиrq в диапазоне Даты 
 ~E просроченные сообщения 
 ~e  EXPR сообщения содержащие  EXPR  в поле lqОтправительrq  
 ~F помеченные сообщения 
 ~f  EXPR сообщения берущие начало из  EXPR 
 ~g сообщения подписанные PGP 
 ~G сообщения шифрованные PGP 
 ~h  EXPR сообщения содержащие  EXPR  в заголовке сообщения 
 ~k сообщение, содержащее текст ключа PGP 
 ~i  EXPR сообщение с соответствием  EXPR  в поле lqmessage-IDrq 
 ~L  EXPR сообщение либо происходящее, либо принятое от  EXPR 
 ~l сообщение адресованное в список известных адресатов 
 ~m  MIN - MAX  сообщение в диапазоне от  MIN  до  MAX 
 ~n  MIN - MAX сообщения со значением в диапазоне от  MIN  до  MAX 
 ~N новые сообщения 
 ~O старые сообщения 
 ~p сообщение адресованные вам (сверяется с $alternates) 
 ~P сообщение от вас (сверяется с $alternates) 
