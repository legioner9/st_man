 НАЗВАНИЕ 

locale - описание файла определения локали
   
 ОПИСАНИЕ 

Файлы определения
 locale 

содержат в себе всю информацию, которую команда
 localedef (1)

требует для преобразования в бинарную базу данных докали.
Файлы определения состоят из разделов, каждый из которых
подробно описывает некоторые категории локали.
   
 СИНТАКСИС 

Файлы определения локали начинаются с заголовка,
который может состоять из следующих ключевых слов:
 
 <escape_char> 

 
сопровождается символом, который будет использоваться как
символ экранирования всей оставшейся части файла и для обозначения
символов, которые должны восприниматься особым образом.
По умолчанию это символ обратной косой черты (
 \\ ).

 <comment_char> 

 
сопровождается символом, который будет использоваться как
символ комментария для всей оставшейся части файла.
По умолчанию это символ номера (#).
 
 

Определение локали имеет часть для каждой категории локали.
Каждая часть может быть скопирована из другой существующей локали 
или может быть скопирована и скорректирована по Вашему желанию.
Если категория должна быть скопирована, то в определении должно находиться
единственное ключевое слово 
 copy 

, сопровождаемое названием локали, из которой скопирована категория.
   
 LC_CTYPE 

Определение для категории
 LC_CTYPE 

начинается с команды
 LC_CTYPE 

в первой колонке.
Существуют следующие разрешенные ключевые слова:
 
 upper 

 
сопровождается списком символов в верхнем регистре. Символы от
 A 

до
 Z 

включаются в класс автоматически.
Не разрешается использовать в списке символы классов:
 cntrl ,

 digit ,

 punct 

или
 space .

 lower 

 
сопровождается списком символов в нижнем регистре. Символы от
 a 

до
 z 

включаются в класс автоматически.
Не разрешается использовать в списке символы классов:
 cntrl ,

 digit ,

 punct 

или
 space .

 alpha 

 
сопровождается списком символов. Все символы, указанные в классах
 upper 

или
 lower ,

автоматически включаются в данный класс.
Не разрешается использовать в списке символы классов:
 cntrl ,

 digit ,

 punct 

или
 space .

 digit 

 
сопровождается списком символов, классифицированных как цифры.
Разрешается использовать в списке только цифры от
 0 

до
 9 

(они включаются в этот класс автоматически);
 space 

 
сопровождается списком символов, классифицированных как пустые символы.
Не разрешается использовать в списке символы классов:
 upper ,

 lower ,

 alpha ,

 digit ,

 graph 

или
 xdigit .

Символы
 <space> ,

 <form-feed> ,

 <newline> ,

 <carriage-return> ,

 <tab> 

и
 <vertical-tab> 

(включаются в этот класс автоматически)
 cntrl 

 
сопровождается списком символов, классифицированных как управляющие символы.
Не разрешается использовать в списке символы из классов:
 upper ,

 lower ,

 alpha ,

 digit ,

 punct ,

 graph ,

 print 

или
 xdigit .

 punct 

 
сопровождается списком символов, классифицированных как символы пунктуации.
Не разрешается использовать в списке символы классов:
 upper ,

 lower ,

 alpha ,

 digit ,

 cntrl ,

 xdigit 

или символ
 <space> .

 graph 

 
сопровождается списком символов для печати, исключая символ
 <space> .

Символы из классов
 upper ,

 lower ,

 alpha ,

 digit ,

 xdigit 

и
 punct 

(включаются в этот класс автоматически).
Не разрешается использовать в списке символы из класса
 cntrl ;

 print 

 
сопровождается списком печатаемых символов, включая символ
 <space> .

Символы из классов
 upper ,

 lower ,

 alpha ,

 digit ,

 xdigit ,

 punct 

и символ
 <space> 

(включаются в этот класс автоматически).
Не разрешается использовать в списке символы из класса
 cntrl .

 xdigit 

 
сопровождается списком символов,
классифицированных как шестнадцатеричные числа.
Далее должны быть указаны десятичные числа, сопровождаемые
одним или несколькими списками из шести символов в возрастающем порядке.
Символы
от
 0 

до
 9 ,

от
 a 

до
 f ,

от
 A 

до
 F  

(включаются в этот список автоматически).
 blank 

 
сопровождается списком символов, классифицированных как
 blank .

Символы
 <space> 

и
 <tab> 

(включаются в этот класс автоматически).
 toupper 

 
сопровождается списком преобразований символов нижнего регистра в символы верхнего.
Каждое преобразование выполняется для пары символов в нижнем и верхнем регистре, 
разделенных
 , 

и закрытых круглыми скобками.
Элементы списка разделяются точками с запятой.
 tolower 

 
сопровождается списком преобразований символов верхнего регистра в символы нижнего.
Если команда tolower не задана, то по умолчанию используется
список, противоположный списку команды toupper.
 
 

