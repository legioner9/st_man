 LC_TIME 

Определение начинается с команды
 LC_TIME 

в первой колонке.
Существуют следующие разрешенные ключевые слова:
 
 abday 

 
сопровождается списком кратких названий дней недели.
Список начинается с Sunday или его перевода (например, воскресенье).
 day 

 
сопровождается списком названий дней недели.
Список начинается с воскресенья.
 abmon 

 
сопровождается списком кратких названий месяцев.
 mon 

 
сопровождается списком названий месяцев.
 am_pm 

 
Соответственное представление строк 
 am 

и
 pm 

  .
 d_t_fmt 

 
Дата и формат времени соответственно.
 d_fmt 

 
Формат даты.
 t_fmt 

 
Формат времени.
 t_fmt_ampm 

 
12-часовой формат времени.
 
 

