 LC_COLLATE 

Категория 
 LC_COLLATE 

определяет правила сортировки символов.
В libc (из-за ее несовершенства) не все параметры POSIX могут быть выполнимы.
Определение начинается с команды
 LC_COLLATE 

в первой колонке.
Для выполнения определения 
существуют следующие допустимые ключевые слова:
 
 collating-element 

 
 collating-symbol 

 
 
 

Определение упорядочивания начинается с команды
 
 order_start 

 
  ,
 
 

сопровождаемой списком команд:
 forward, 

 backward 

или
 position. 

Описание упорядочивания состоит из строк, описывающих метод упорядочивания.
Описание заканчивается командой
 
 order_end. 

 
 
 

Для более подробной информации смотрите исходные варианты в
 /usr/lib/nls/src 

,
особенно, примеры
 POSIX: 

 Примеры (Example) 

и
 Примеры2 (Example2). 

 

Определение для категории
 LC_COLLATE 

заканчивается строкой
 END LC_COLLATE. 

   
 LC_MONETARY 

Определение начинается с команды
 LC_MONETARY 

в первой колонке.
Это выполняется с помощью следующих разрешенных ключевых слов:
 
 int_curr_symbol 

 
(сопровождаются символом международной валюты).
Это должна быть строка из четырех символов,
в которой указан символ международной валюты согласно
стандарту ISO 4217: три символа с последующим разделителем.
 currency_symbol 

 
сопровождается локальным валютным символом.
 mon_decimal_point 

 
сопровождается строкой, которая будет использована в качестве
разделителя (десятичной точки) при выводе денежных сумм.
 mon_thousands_sep 

 
сопровождается строкой, которая будет использована в качестве
разделителя тысяч при выводе денежных сумм.
 mon_grouping 

 
сопровождается строкой, которая описывает форматирование числовых количеств.
 positive_sign 

 
сопровождается строкой, которая используется для указания
положительного знака денежных сумм.
 negative_sign 

 
сопровождается строкой, которая используется для указания
отрицательного знака денежных сумм.
 int_frac_digits 

 
сопровождается количеством дробных цифр,
которые должны использоваться при форматировании с
 int_curr_symbol. 

 frac_digits 

 
сопровождается количеством дробных цифр,
которые должны использоваться при форматировании с
 currency_symbol. 

 p_cs_precedes 

 
сопровождается целым числом, установленное значение которого равно
 1 

  ,
если
  символ
 currency_symbol 

или
 int_curr_symbol 

должен предшествовать форматированному денежному количеству;
или равно
 0 

  ,
если символ следует за его величиной.
 p_sep_by_space 

 
сопровождается целым числом.
 
 
 0 

 
означает, что между символом и величиной не должны пропечатываться никакие знаки.
 1 

 
означает, что между символом и величиной должен печататься пробел.
 2 

 
означает, что пробел должен печататься между символом и знаковой
строкой, если они смежны.
 
 

 n_cs_precedes 

 
 
 
 0 

 
: 
символ следует за величиной
 1: 

 
символ предшествует величине
 
 

 n_sep_by_space 

 
Устанавливаемое целое число равно
 нулю 

  ,
если никакие пробелы не отделяют
 currency_symbol 

или
 int_curr_symbol 

от величины (в случае с отрицательным денежным значением); оно равно
 единице ,

если пробел отделяет символ от величины; и равно
 двум ,

если пробел отделяет символ от знаковой строки (в случае их смежности).
 p_sign_posn 

 
 
 
 0 

 
Круглые скобки включают в себя количество и
 currency_symbol 

или
 int_curr_symbol. 

 1 

 
Знаковая строка предшествует количеству и
 currency_symbol 

или
 int_curr_symbol. 

 2 

 
Знаковая строка следует за количеством и
 currency_symbol 

или
 int_curr_symbol. 

 3 

 
Знаковая строка предшествует количеству и
 currency_symbol 

или
 int_curr_symbol. 

 4 

 
Знаковая строка следует за
 currency_symbol 

или
 int_curr_symbol. 

 
 

 n_sign_posn 

 
 
 
 0 

 
Круглые скобки включают в себя количество и
 currency_symbol 

или
 int_curr_symbol. 

 1 

 
Знаковая строка предшествует количеству и
 currency_symbol 

или
 int_curr_symbol. 

 2 

 
Знаковая строка следует за количеством и
 currency_symbol 

или
 int_curr_symbol. 

 3 

 
Знаковая строка предшествует
 currency_symbol 

или
 int_curr_symbol. 

 4 

 
Знаковая строка следует за
 currency_symbol 

или
 int_curr_symbol. 

 
 

 
 

