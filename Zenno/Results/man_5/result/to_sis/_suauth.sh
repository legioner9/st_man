 НАЗВАНИЕ 

suauth - файл конфигурации su
 
   
 СИНТАКСИС 

 /etc/suauth 
 
   
 ОПИСАНИЕ 

Файл  /etc/suauth  просматривается всякий раз когда
запускается команда su . Он может изменить поведение
команды su основаное на
 
1) the user su is targetting
 
2) пользователь выполняет команду su (или любая
группа , которой он может</p>
быть членом
 
 
 Строки  
 
начинающиеся с # считаются кометариями и
игнорируются. Строка этого файла имеет вид:
 
to-id:from-id:ACTION
 
Где to-id есть или слово  ALL , или список пользователей
разделенный "," или слова  ALL EXCEPT  за которыми следует
список пользователей разделенный ","
 
from-id организован таким же образом как и to-id, исключая
слово  GROUP  которое тоже можно использовать.  ALL EXCEPT
GROUP  тоже используется. Следующие строки за  GROUP 
показывают одну или более имен групп, разделенных ",". Не
достаточно иметь идентификатор группы (primary group id)
относящейся к делу группы в /etc/ group (5) 
 
 Акция (ACTION) может быть только одна из следующих 
 
поддерживаемых опций.
 
 DENY (отказывать)  Попытка запуска su будет остановлена
перед запросом пароля.
 
 NOPASS (без пароля)  Попытка запуска su будет автоматичекси
удачной; без запроса пароля
 
 OWNPASS (пароль владельца) 
 
Для того что бы команда su выполнилась
удачно, пользователь должен ввести
свой пароль.
 
 Заметте  
 
что в файле должны быть три поля разделенных
знаком ":". Знака пробела не должно быть между ними. Так
же заметте что файл проверяется строка за строкой , и
используется первое встреченое правило , без дальнейшей
проверки файла
 
 ПРИМЕРЫ 
 
# простой /etc/suauth файл
#
# Пара привелигированных пользователей могут пользоваться
# программой su для доступа к аккаунту root с использованием
# своих паролей .
#
root:chris,birddog:OWNPASS
#
# Все остальные не могут использовать прогррамму su
# для доступа к аккаунту to root, исключение составляет
# группа wheel. This is how BSD does things.
#
root:ALL EXCEPT GROUP wheel:DENY
#
# Возможно terry и birddog это аккаунты
# одного и того же владельца.
# Доступ от одного к другому может быть оссуществлен
# без пароля.
#
terry:birddog:NOPASS
birddog:terry:NOPASS
#
 
 
   
