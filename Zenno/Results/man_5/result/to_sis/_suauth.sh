 ОШИБКИ 

Возможно есть большое множество необнаруженых ошибок. При
разборке файла могут встретиться ошибки не приводящие к
синтаксической ошибке , исключения составляют пробелы (не
смотря на их расположение в начале или в конце сторки), и
определенные последовательности разделенные различными
разделителями.
 
   
 DДИАГНОСТИКА 

 
Ошибки разбора файла можно обнаружить с помощью  sys logd (8) 
уровня ERR on facility AUTH.
 
   
