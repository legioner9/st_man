 ОШИБКИ 
 
Исходная метка диска, возвращаемая  ccd(4)  содержит всего 3 раздела.
Необходимо увеличить количество разделов до 8 с помощью команды " disklabel -e ", чтобы привести
метку к типичному для BSD виду. 

