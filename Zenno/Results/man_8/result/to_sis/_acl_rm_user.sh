 acl_rm_user (8) >>  acl_rm_user  (8)   ( Русские man: Команды системного администрирования ) 
   
 НАЗВАНИЕ 

 

 acl_rm_user  - Средство управления ACL-записями RSBAC для пользователей.
   
 СИНТАКСИС 

 

 acl_rm_user   -y  user
   
 ОПИСАНИЕ 

 

Программа  acl_back_user  предназначена для удаления ACL-записей RSBAC у заданных пользователей.
   
 ПАРАМЕТРЫ 

 

Параметр  -y  служит для отмены ожидания программой подтверждение операции со стороны пользователя.
   
 ПРИМЕРЫ 

 

acl_rm_user -y user1
 

удалит не ожидая подтверждения со стороны пользователя все ACL-записи для пользователя user1
 
 
   
