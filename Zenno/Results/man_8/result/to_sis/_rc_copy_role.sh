 rc_copy_role (8) >>  rc_copy_role  (8)   ( Русские man: Команды системного администрирования ) 
   
 НАЗВАНИЕ 

 rs_copy_role  - Работа с ролями RC модуля системы RSBAC.
   
 СИНТАКСИС 

 rc_copy_role  from_role to_role
 
   
 ОПИСАНИЕ 

 rc_copy_role  копирует одну RC-роль в другую.
   
 ПАРАМЕТРЫ 

 

Параметрами для  rs_copy_role  являются:
 
 from_fole  - Числовой идентификатор исходной роли-шаблона. 
 to_role  - Числовой идентификатор целевой роли. 
 
   
