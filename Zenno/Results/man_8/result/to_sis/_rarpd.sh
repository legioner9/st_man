 БЕЗОПАСНОСТЬ 

 

 rarpd  требует привилегии CAP_NET_RAWIO
для прослушивания и отправки пакетов RARP и ARP. Он также требует привилегии CAP_NET_ADMIN,
чтобы дать указание ядру для разрешения ARP; это не требуется строго,
но многие клиенты (если точнее, большинство) настолько плохо работают с этим, что
не способны ответить ARP прежде, чем они окончательно загрузятся.
Неважно, что клиенты используют RARPD 2002-ого года
со всеми не поддерживаемыми архаизмами 90-ых и более ранних.
   
