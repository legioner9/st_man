 ИМЯ 

rarpd - отвечает на запросы RARP
   
 СИНТАКСИС 

 
 arping  [  -aAvde ]  [  -b  bootdir ]  [  interface ] 
 
   
 ОПИСАНИЕ 

 

Ожидание
RARP
запросов от клиентов. Если указанный MAC-адрес клиента
найден в базе данных  /etc/ethers  и
полученное имя хоста соответствует IP-адресу
для данной сети,  rarpd  отправляет сообщение клиенту сообщение RARPD
с IP-адресом.
 

Для разрешения множества загрузочных серверов в сети  rarpd 
опционально проверяет на наличие Sun-подобных загрузочных образов в каталоге TFTP.
Оно должно иметь форму  Hexadecimal_IP.ARCH , т.е загружать
sparc 193.233.7.98  C1E90762.SUN4M  ссылающегося
на соответствующий образ для SUM4M в каталоге  /etc/tftpboot .
   
 ВНИМАНИЕ 

 

Это давно устаревшая опция
BOOTP
и более поздних
DHCP протоколов.
Однако, многие клиенты всё ещё требуют её для загрузки.
   
 ОПЦИИ 

 
 -a 
Прослушивает на всех интерфейсах сети. На данный момент она
присутствует как внутренняя опция, эта функция подменяется аргументом  interface . 
Она не должна использоваться.
 -A 
Прослушивает не только RARP, но также и ARP сообщения, многие устаревшие клиенты
используют ARP по неизвестным причинам.
 -v 
Выводить более подробные сведения.
 -d 
Режим отладки. Не проводите в фоновом режиме.
 -e 
Не проверять на наличие загрузочного образа, сообщать если MAC-адрес
разрешает правильным IP-адресам использование базы  /etc/ethers 
и DNS. 
 -b  bootdir 
Каталог загрузочных образов TFTP. По умолчанию  /etc/tftpboot 
 
   
