 ДИАГНОСТИКА 

Команда  grpck  завершает работу со следующими значениями:
 
 0 
Нормальное завершение работы
 1 
Синтаксическая ошибка
 2 
Одна (или несколько) поврежденных записей
 3 
Невозможно открыть файл групп
 4 
Невозможно заблокировать файл групп
 5 
Невозможно обновить файл групп
 
   
