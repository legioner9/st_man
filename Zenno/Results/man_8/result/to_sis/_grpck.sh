 НАЗВАНИЕ 

grpck - проверяет целостность системного файла групп
   
 СИНТАКСИС 

 grpck  [ -r ] [ group   shadow ]
   
 ОПИСАНИЕ 

 grpck  проверяет целостность системной информации об авторизации.
Все элементы в файлах  /etc/group  и  /etc/gshadow  проверяются
на корректный формат и соответствующие полям данные.
У пользователя запрашивается подтверждение на удаление
ошибочных и поврежденных записей, не подлежащих восстановлению.

Анализом данных считается проверка каждого элемента на
 

- корректный номер поля
 

- уникальное имя группы
 

- корректный список членов и администраторов

 

Анализ корректности номеров в полях и уникальности имен групп является
самым приоритетным. Если будет найдена ошибка в этих элементах, то
у пользователя запросится подтверждение на удаление целой строки.
Если пользователя ответит отрицательно, то все последующие
проверки отменяются. Для записей с дублированными именами групп
запрашивается подтверждение на удаление, но в случае отрицательного ответа
будет продолжена дальнейшая проверка.
Все остальные ошибки будут выведены только на уровне предупреждений и
сообщений, и пользователю необходимо будет в дальнейшем запускать программу
 groupmod  для исправления ошибок.

Команды, работающие с файлом  /etc/group , не должны изменять
поврежденные и дулированные записи.
В таких случаях неоходимо использовать  grpck  для
удаления некорректных записей.
   
 ОПЦИИ 

По умолчанию  grpck  работает с фалами  /etc/group  и
 /etc/gshadow . Но пользователь может указать на альтернативные
файлы в параметрах  group  и  shadow . Также пользователь может
исполнять команду в режиме только-чтение, определив параметр  -r .
Это означает, что на все запросы об изменениях автоматически будет
отвечаться  нет/no .
   
 ФАЙЛЫ 

/etc/group - информация об учетных записях групп
 

/etc/gshadow - зашифрованные пароли и информация об администраторах групп
 

/etc/passwd - информация о пользователях
   
