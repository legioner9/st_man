 ПАРАМЕТРЫ КОНФИГУРАЦИИ 


 

Для этой программы особо актуальны следующие параметры из  main.cf .
Подробнее о синтаксисе и значениях по умолчанию смотрите в файле
 main.cf . После изменения конфигурации выполните команду
 postfix reload .
   
 Фильтрация содержания 

 
 body_checks 
Поисковые таблицы с фильтрами содержания для строк тела сообщения.
Эти фильтры просматривают физические строки по одной за раз, размером
не более line_length_limit байт.
 header_checks 
Поисковые таблицы с фильтрами содержания для строк заголовка сообщения.
Эти фильтры просматривают логические заголовки по одному за раз,
включая заголовки, которые занимают несколько строк.
 
   
 Разное 



 
 always_bcc 
Адрес, на который будут отправляться копии каждого сообщения, 
поступающего в систему.
 hopcount_limit 
Максимальное количество заголовков сообщения  Received: .
 undisclosed_recipients_header 
Строка заголовка, вставляемая при отстутствии получателей в
заголовках сообщения (Resent-)To: или (Resent-)Cc:.
 
   
 Преобразования адреса 



 
 empty_address_recipient 
Получатель не доставленной почты от <>. Эта замена выполняется
до всех остальных перезаписей адреса.
 canonical_maps 
Поисковая таблица подмены адресов отправителя и получателя в
конвертах и заголовках.
 recipient_canonical_maps 
Поисковая таблица подмены адресов получателя в конверте и 
заголовке.
 sender_canonical_maps 
Поисковая таблица подмены адресов отправителя в конверте и
заголовке.
 masquerade_domains 
Список доменов, скрывающих свою структуру поддоменов.
 masquerade_exceptions 
Список имён пользователей, на которые не распространяется
подмена адресов.
 virtual_maps 
Поисковая таблица подмены адресов получателей в конверте.
 
   
 Управление ресурсами 



 
 duplicate_filter_limit 
Максимальное количество запоминаемых получателей, указанных 
в конверте.
 header_size_limit 
Максимальное количество памяти в байтах, используемое для
обработки заголовка сообщения.
 extract_recipient_limit 
Максимальное количество получателей, извлекаемых из заголовков
сообщения.
 
   
