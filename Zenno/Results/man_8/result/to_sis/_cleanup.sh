 НАЗВАНИЕ 

cleanup
-
канонизация и постановка в очередь сообщения Postfix
   
 ОБЗОР 


 cleanup  [общие параметры демона Postfix]
   
 ОПИСАНИЕ 



Демон  cleanup  обрабатывает входящую почту, помещает её
в почтовую очередь  cleanup  и информирует менеджера очереди
об её прибытии.
 
Демон  cleanup  всегда выполняет следующие преобразования:
 
 . 
Вставку отсутствующих заголовков сообщения: ( Resent- ) 
 From: ,  To: ,  Message-Id:  и  Date: .
 . 
Извлечение адресов получателей из заголовков сообщения
( Resent- )  To: ,  Cc:  и  Bcc:  в конверте, 
если в конверте сообщения получатели не указаны.
 . 
Преобразует адреса в конверте и заголовке в стандартную форму
 user@fully-qualified-domain , которую ожидают другие программы
Postfix.
Эта задача поручается демону  trivial-rewrite (8).
 . 
Устраняет дублирование адресов получателей в конверте.
 
 

Следующие преобразования адреса являются дополнительными:
 
 . 
Дополнительно, переписывает все адреса в конверте и заголовке в
соответствии с правилами подмены, определенными в поисковых таблицах
 canonical (5).
 . 
Дополнительно, маскирует адреса отправителей в конверте и адреса
в заголовке сообщения (т.е. удаляет информацию о машине или домене
ниже всех доменов, перечисленных в параметре 
 masquerade_domains , исключая имена пользователей, 
перечисленных в  masquerade_exceptions ). Маскарад адреса
не распространяется на получателей, указанных в конверте.
 . 
Дополнительно указывает в развёрнутом виде получателей, указываемых в конверте, в
соответствии с информацией, найденной в поисковых таблицах
 virtual (5).
 
 

Демон  cleanup  выполняет проверку разумности содержимого каждого
сообщения. При обнаружении проблем он, по умолчанию, возвращает
диагностические сообщения клиенту и оставляет клиента самого разбираться
с проблемой. С другой стороны, в случае проблемы клиент может 
запросить демон  cleanup  вернуть сообщение назад отправителю.
   
