 ИМЯ 
 

 

pppoe-status - сценарий просмотра состояния канала PPPoE
 

 

 ОБЗОР 
 

 

pppoe-status [файл_конфигурации]
 

 

 ОПИСАНИЕ 
 

 

pppoe-status - это сценарий оболочки, который проверяет состояние PPPoE-каналов, установленных PPPoE-клиентом Roaring Penguin, работающим в пространстве пользователя. Если вы опустили файл_конфигурации, по умолчанию будет использован файл  /etc/ppp/pppoe.conf .
 

 

 АВТОР 
 

 

pppoe-status был написан Дэвидом Ф. Сколлом (David F. Skoll)  dfs@roaringpenguin.com .
 

 
Домашняя страница pppoe -  http://www.roaringpenguin.com/pppoe/ .
 

 

