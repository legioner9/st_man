 ИМЯ 
 

 

pppoe-setup - сценарий для настройки PPPoE-клиента Roaring Penguin
 

 

 ОБЗОР 
 

 

pppoe-setup
 

 

 ОПИСАНИЕ 
 

 

pppoe-setup - это сценарий оболочки, который запрашивает у вас различные части информации и устанавливает файл конфигурации  /etc/ppp/pppoe.conf  для сценариев  pppoe-start ,  pppoe-stop  и  pppoe-connect .
 

 

