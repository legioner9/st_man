 rsbac_check (8) >>  rsbac_check  (8)   ( Русские man: Команды системного администрирования ) 
   
 НАЗВАНИЕ 

 rsbac_check  - Работа со статистикой RSBAC.
   
 СИНТАКСИС 

 rsbac_check  correct check_inodes
 
   
 ОПИСАНИЕ 

 rsbac_check  устанавливает уровень проверки последовательности структур данных.
 
   
 ПАРАМЕТРЫ 

 correct  - исправления
 

 0  = не исправлять ошибки
 

 2  = исправлять ошибки
 

 2  = исправлять все ошибки полностью
 
 check_inodes  - проверка инодов 
 

 0  = не проверять номера инодов
 

 1  = проверять номера инодов
 
   
