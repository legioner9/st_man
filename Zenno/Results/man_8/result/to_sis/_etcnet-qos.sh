/etc/net руководство по QoS
   
 ОПИСАНИЕ 

Основные правила:
 
 1. Классы и Q-диски могут содержать друг друга. Обычно класс содержит q-диск или один или более подкласс. Q-диск обычно содержит один или более класс. 
 2. Содержимое представлено в виде дерева каталогов. 
 3. Каталог должен содержать один из двух файлов: либо 'class', либо 'qdisc', так, чтобы /etc/net знал тип каждого узла. Файл содержит текущие параметры узла. 
 4. Дескриптор q-диска - это имя каталога с ':'. 
 5. Класс classid - это последний q-диск, замеченный в дереве, плюс ':' плюс имя каталога класса. 
 6. Если текущий каталог содержит файл 'extra', его содержимое будет использоваться при создании дочерних объектов (обычно классов). Это не относится к дочерним объектам в дочерних объектах. 
 7. Класс dir может содержать файл 'filter' с установками фильтра. Фильтры будут связаны с родительским q-диском и указывать на текущий каталог (класс), вы не должны определять ключевые слова parent и flowid. Если файл 'extra.filter' существует, его содержимое будет ставиться перед каждой строкой файла 'filter'. 
 
   
