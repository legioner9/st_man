 ПРИМЕЧАНИЯ 

 Нет возможности запросить n-ное вхождение файла. 

 Текущее ограничение на длину имени файла - 100 символов. 

 Поскольку утилита  cpio  может распознавать архивы, сформированные с 
помощью  tar , ее можно использовать как альтернативную команду для чтения 
архивов  tar . 

 В системе, предшествующей Release 4, нельзя восстановить архивы каталогов, 
подкаталогов или файлов в каталогах, созданные с помощью команды  tar  версии 
UNIX System V Release 4. (То есть, можно восстановить только файлы на уровне 
текущего каталога.) При попытке извлечь подкаталоги будут получены 
сообщения об ошибках, утверждающие о невозможности создания файлов. 

 Использование режима переменной длины блока при записи на магнитные 
ленты не рекомендуется, поскольку может работать некорректно в релизах, 
предшествующих SVR4.2 MP. Магнитные ленты должны всегда записываться в 
режиме с фиксированной длиной блока, хотя вы свободно можете изменить 
стандартную длину блока с 512 байтов на любое фиксированное значение, 
поддерживаемое приводом ленты (стримером). 

 Чтение с магнитной ленты блоками фиксированной длины, отличной от длины 
блока, использованной при записи носителя, вызовет ошибку ввода/вывода. 
Если необходимо прочитать ленту, записанную с длинной блока, отличной от 
стандартной 512, вы должны использовать команду  tapecntl(1)  (qv), чтобы либо 
установить размер блока для привода равным длине блока носителя, либо 
перевести привод в режим переменной длины блока. 

