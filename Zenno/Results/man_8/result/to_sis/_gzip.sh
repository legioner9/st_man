 ПОТЕНЦИАЛЬНЫЕ ПРОБЛЕМЫ 

 При записи сжатых данных на ленту обычно необходимо дополнять
результат нулями до границы блока. При чтении данных и передаче их
для распаковки утилите  gunzip , она выявляет дополнительный "мусор"
после сжатых данных и, по умолчанию, выдает предупреждение. Чтобы
подавить выдачу этого предупреждения, используйте опцию  --quiet .
Эту опцию можно задать в переменной среды  GZIP , как в следующем
примере: 
 
для  sh :  GZIP="-q"; tar -xfz --block-compress /dev/rst0 
для  csh :  (setenv GZIP -q; tar -xfz --block-compr /dev/rst0) 
 

 В примере выше утилита  gzip  вызывается неявно опцией  -z 
утилиты GNU  tar . Проверьте, что при чтении и записи сжатых данных на
ленты используется один и тот же размер блока (опция  -b 
программы  tar ). (В этом примере предполагается использование
GNU-версии программы  tar .) 

 ОШИБКИ 

 Опция  --list  выдает некорректные размеры, если они превышают 2 Гбайта.
Опция  --list  выдает значение размера  -1  и контрольной суммы crc - 
 ffffffff , если сжатый файл находится на носителе, не допускающем
произвольную адресацию (non seekable media). 

 В некоторых редких случаях опция  --best  дает худшее сжатие, чем стандартный
уровень сжатия ( -6 ). Некоторые особо избыточные файлы сжимаются утилитой 
 compress  лучше, чем утилитой  gzip . 

