 ДИАГНОСТИКА 

 Статус выхода обычно имеет значение 0; если возникает ошибка,
статус выхода имеет значение 1. Если выдавались предупреждения,
статус выхода имеет значение 2. 
 
 Usage: gzip [-cdfhlLnNrtvV19] [-S suffix] [file ...] 
В командной строке заданы недопустимые опции.
 
 file: not in gzip format 
Указанный программе  gunzip  файл не был сжат утилитой  gzip .
 
 file: Corrupt input. Use gzcat to recover some data. 
Сжатый файл был поврежден. Данные до точки сбоя могут быть восстановлены
с помощью команды
 
	gzcat file > recover
 
 
 file: compressed with xx bits, can only handle yy bits 
Файл был сжат (с помощью алгоритма LZW) программой, способной работать
с большим количеством битов, чем код распаковки на данной машине.
Сожмите файл заново с помощью утилиты  gzip , сжимающей лучше и
использующей меньше памяти.
 
 file: already has .gz suffix -- no change 
Предполагается, что файл уже сжат. Переименуйте файл и попробуйте еще раз.
 
 file already exists; do you wish to overwrite (y or n)? 
Ответьте утвердительно ( y ), если хотите заменить существующий
файл, или отрицательно ( n ), если не хотите.
 
 gunzip: corrupt input 
Был получен сигнал  SIGSEGV , что обычно вызвано повреждением входного
файла.
 
 xx.x% 
Процент входного файла, сэкономленный за счет сжатия.
(Только для опций  -v  и  -l .)
 
 -- not a regular file or directory: ignored 
Если входной файл не является обычным файлом или каталогом, 
(т.е. это символическая связь, сокет, именованный канал FIFO или 
специальный файл устройства, он не изменяется.
 
 -- has xx other links: unchanged 
Входной файл имеет несколько связей; он не изменняется. Поробнее
о связях см. на странице справочного руководства  ln(1) . Для
принудительного сжатия файлов с несколькими связями используйте
флаг  -f .
 
 

