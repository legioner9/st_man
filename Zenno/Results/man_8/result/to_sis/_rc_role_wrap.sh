 rc_role_wrap (8) >>  rc_role_wrap  (8)   ( Русские man: Команды системного администрирования ) 
   
 НАЗВАНИЕ 

 rs_role_wrap  - Работа с ролями RC модуля системы RSBAC.
   
 СИНТАКСИС 

 rc_role_wrap   [-v]  new_role prog args 
 
   
 ОПИСАНИЕ 

 rc_role_wrap  запускает программу с другой (указанной в аргументах) RC ролью.  
 
   
 КЛЮЧИ 

 [-v]  - выводить подробную информацию.
 
 new_role  - роль, с которой будет выполнена программа. 
 prog  - имя программы. 
 args  - параметры командной строки для программы  prog . 
 
 
   
 СМ. ТАКЖЕ 

 

 RSBAC  - Rule Set Based Access Control,  pm_ct_exec (8),  mac_wrap (8).
 
   
