 ОПИСАНИЕ 

 dpkg  - это инструмент среднего уровня для инсталляции,
построения, удаления и управления пакетами Debian
GNU/Linux. Первичная и более дружественная облочка для
 dpkg  - это  dselect (8)

параметры командной строки, которые включают одну или
много опций и одно действие. Параметр-действие говорит
dpkg  что ему нужно сделать, а опции управляют выполнением
действия.
 
 dpkg  может также использоваться как средство для работы с
 dpkg-deb.  Действия  -b  ,  --build  ,  -c  ,  --contents  ,  --I  ,
 --info  ,  -f  ,  --field  ,  -e  ,  --control  ,  -x  ,  --extract  ,
 -X  ,  --vextract  и  --fsys-tarfile  это действия  dpkg-deb  и
если они встречаются,  dpkg  запускает  dpkg-deb  с
параметрами указанными для  dpkg.  Читайте  dpkg-deb (8) 
  для
получения информации об этих действиях.
 
 ДЕЙСТВИЕ
 
dpkg -i | --install <имя файла пакета> ... 
Установить заданный пакет. Если задается опция
 --recursive  или  -R  , то вместо  имя файла пакета 
должен быть указан каталог.
 
 
 Установка состоит из следующих шагов: 
 
 1.  Извлечение файлов управления из пакета.
 2.  Если другая версия этого пакета была
установлена ранее, то запускается сценарий  prerm 
для старого пакета.
 3.  Запускается сценарий  preinst  , если он есть в
данном пакете.
 4.     Распаковываются файлы и в это же время
происходит резервное сохранение старых файлов так
что если что-либо пойдет не так, мы можем
восстановить их.
 5.  Если другая версия данного пакета была
установлена ранее, то запускаем сценарий  postrm  для
старого пакета. Заметим, что этот сценарий
запускается после сценария  preinst  нового пакета,
потому что новые файлы пишутся в то же время, когда
старые удаляются.  6.  Настройка пакета. Смотрите
 --configure  для более подробной информации об этом
шаге.
 
 dpkg --unpack <имя файла пакета> ... 
 
Распаковать пакет, но не конфигурировать его. Если
задана опция  --recursive  или  --R  , то вместо  имя
файла пакета  должен быть указан каталог.
 
 dpkg --configure <имя пакета> ... | -a|--pending 
Перенастроить распакованный пакет. Если вместо  имя
пакета  задана опция  -a  или  --pending  , то все
распакованные, но не настроенные пакеты будут
настроены.
 
 Настройка состоит из следующих шагов: 
 
 1.  Распаковка конфигурационных файлов и в это же
время сохранение старых кофигурационных файлов, так
что мы можем восстановить их, если что-то пойдет не
так.
 2.  Запуск сценария postinst, если он есть в данном
пакете.
 
 dpkg -r|--remove | --purge <имя пакета> ... | -a|--pending 
Удалить установленный пакет.  --purge удаляет все, 
 
 вместе  
 
      с      конфигурационными       файлами. 
(конфигурационные файлы это те, что указаны в
управляющем файле  confiles  ). Если вместо  имя
пакета  задана опция  -a  или  --pending,  то все
распакованные пакеты, но отмеченные к удалению или
к очищению(purge) удаляются или очищаются (в файле
 /var/liba/dpkg/status  ). Удаление пакета состоит
из следующих шагов:
 1.  Запуск сценария  prerm
2.  Удаление установленных фалов
 3.  Запуск сценария  postrm
 
dpkg -A | --avail <имя файла пакета> ... 
Обновить представление  dpkg  и  dselect  о тех
пакетах, в которых доступна информация о пакете  имя
файла пакета.  Если задана опция  --recursive  или
 --R,  то вместо  имя файла пакета  должен быть указан
каталог.
 
 dpkg --update-avail | --merge-avail <Файл Packages> 
Обновить предстваление  dpkg  и  dselect  о тех
пакетах, что доступны. С действием  --merge-avail 
информация комбинируется из старой информации и
файла  Packages.  С действием  --update-avail  старая
информация заменяется на информацию из файла  Pack_ a ges. 
Файл  Packages  распостраняется с Debian
GNU/Linux и обычно называется Packages.
 
 dpkg --yet-to-unpack 
 
Ищет пакеты выбранные к установке, но которые по
каким-либо причинам не были установлены.
 
 dpkg -l|--list [<образец имени пакета> ...] 
Показывает пакеты соответствующие данному образцу.
Если  образец имени пакета  не задан, показывает все
пакеты в  /var/lib/dpkg/avialable.  В  образце имени
пакета  можно указывать `*' и `?'.
 
 dpkg -L|--listfiles <имя пакета> ... 
 
Показывает файлы установленные в вашей системе из
пакета c названием  имя пакета.  Однако заметим, что
файлы созданные специальными сценариями данного
пакета показаны не будут.
 
 dpkg -C|--audit 
 
Ищет пакеты, что были установлены только частично.
 dpkg  посоветует что делать с ними, чтобы заставить
их работать.
 
 dpkg -S|--search <образец имени файла для поиска> ... 
Ищет имя файла по образцу в установленных пакетах.
В образце допустимы символы `*' и `?'. (Для того
чтобы отыскать какому пакету принадлежит файл.
Прим. переводчика)
 
 dpkg -s|--status <имя пакета> ... 
 
Выводит состояние заданного пакета. Это действие
показывает информацию об установленном пакете из
базы состояний пакетов.
 
 dpkg --help 
 
Показвает небольшую подсказку.
 
 dpkg --licence 
 
Показывает лицензию на  dpkg.
 
dpkg --version 
 
Показыает информацию о версии.
 
 dpkg-deb-actions 
 
Смотрите  dpkg-deb (8) 
  для более подробной информации
об этих действиях.
 
 dpkg -b|--build <каталог> [<имя файла>]  - Строит
пакет Debian GNU/Linux.
 
 dpkg -c|--contents <имя файла>  - Показывает
содержимое пакета GNU/Linux.</p>
 dpkg -e|--control <имя файла> [<каталог>]  Извлекает
контрольную информацию из пакета.
 dpkg -x|--extract <имя файла> <каталог>  - Извлекает
файлы содержащиеся в данном пакете.
 dpkg -f|--field <имя файла> [<управляющее поле>...] 
- Показывает управляющее поле или поля пакета.
 dpkg --fsys-tarfile <имя файла>  - Показывает
файловую систему tar-файла, содержащегося в пакете
Debian.</p>
 dpkg -I|--info <имя файла> [<файл control>]  Показывает
информацию о даннои пакете.</p>
 dpkg -X|--vextract <имя файла> <каталог>  Извлекает
и показывает имена файлов, содержащиеся в
пакете.
 
 ОПЦИИ
 
-B | --auto-deconfigure </p>
Если пакет удаляется, то возможно, что другой
установленный пакет зависит от удаляемого пакета.
Задание этой опции приведет к автоматической
денастройке пакета, который зависит от удаляемого
пакета.
 
 -Dh |  <bf>--debug="help</bf"> 
 
Выдает помощь о опциях отладки
 
 -D<octal> |  <bf>--debug="<octal></bf"> 
 
Включение отладки.  octal  - это восьмеричное число,
сформированное побитно из битовых масок, указанных
в списке ниже (заметим, что эти величины могут быть
изменены в следующих версиях). Маска Описание
 
 1  
 
     Основная полезная информация по ходу
выполнения
 
 2  
 
Вызов и состояния сценариев обслуживания
 
