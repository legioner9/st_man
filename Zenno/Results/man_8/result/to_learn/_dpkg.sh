Вывод по каждому конфигурационному файлу
 
 200  
 
        Подробный вывод по каждому
конфигурационному файлу
 
 40  
 
Зависимости и конфликты
 
 400  
 
     Подробное освещение зависимостей и
конфликтов
 
 1000  
 
     Подробная ерунда о всяких dpkg/info
каталогах
 
 2000  
 
Сумасшедшая ерунда
 
 --force-<things> | --no-force-<things> | --refuse-<things> 
 
Включение или отказ (no-force и refuse состояния
для тех же things) некоторых вещей для действий.
 things  -это тот или иной модификатор из отдельного
списка, приведенного ниже:
 
 downgrade(*): 
 
Установить пакет, даже если уже установлена его
более новая версия.
 
 configure-any: 
 
Настроить также распакованные, но не настроенные
пакеты, что зависят от текущего пакета.
 
 remove-reinstreq: 
 
Удалить пакет, даже если он побит и отмечен
требованием к переустановке. Это может, например,
привести к тому, что части пакета остануться в
системе, а затем  dpkg  о них забудет.
 
 hold:  Не беспокоится, если пакет в состоянии hold или
нет.
 
 remove-essential: 
 
Удалить даже если пакет важен. Важные пакеты
содержат базовые команды unix и все такое прочее.
Удаление такого пакета может привести к останову
работы системы, так что используйте данную опцию с
осторожностью.
 
 conflicts: 
 
Установит, даже если пакет конфликтует с другими.
Это опасно, так как обычно приводит к перезаписи
некоторых файлов.
 
 depends: 
 
Удалить, даже если другой пакет зависит от этого.
Это обычно приводит к побитости другого пакета.
 
 depends-version: 
 
Не беспокоится о версиях, когда проверяются
зависимости. Это обычно приводит к побитости
другого пакета.
 
 Модификаторы помеченные (*) включены по умолчанию. 
 
 ВНИМАНИЕ: 
 
эти опции рекомендуется использовать только
экспертам. Их использование без полного понимания
их эффекта может разрушить вашу систему.
 
 --ignore-depends=<пакет>,... 
 
Игнорировать проверку зависимостей для заданных
пакетов (тем не менее, проверка выполняется, но
выдаются только предупреждения о конфликтах и
ничего в других случаях).
 
 --largemem | --smallmem 
 
Говорит  dpkg  чтобы он сохранял память или
использовал ее как понадобится.
 
 --new | --old 
 
Выбирает новый или старый формат пакета. Это опция
 dpkg-deb (8)
 

 
 --nocheck 
 
Не читать или проверять содержимое файла управления
пока строится пакет. Это опция  dpkg-deb (8)
 

 
 --no-act 
 
Делать все, что необходимо, но не записывать любые
изменения. Это используется для просмотра,
выполнения заданных действий без модифицирования
чего-либо.
 
Убедитесь что задали --no-act перед действием параметром
или вы
 
можете закончить с непредсказуемыми результатами.
(т.е.  dpkg --purge foo --no-act  сперва выполнит
очистку пакета foo и затем будет пытаться выполнить
очистку пакета --no-act, несмотря на то, что вы
ожидали, что действий никаких не будет)
 
 -R | --recursive 
 
Рекурсивное управление всеми файлами, отвечающими
шаблону *.deb, которые были найдены в заданных
каталогах и во всех их подкаталогах. Это может
быть использовано с действиями  -i, -A, --install,
--unpack, --avail. 
 
 -G   
 
     Не устанавливать пакет, если новая версия такого же
пакета уже установлена.
 
 -R|--root=<dir> |  <em>--admindir="<dir></em">  |  <em>--instdir="<dir></em"> 
 
Изменяет директории по умолчанию. admindir по
умолчанию в  /var/lib/dpkg  и содержит много файлов,
в которых дается информация о состояниях
установленных или деустановленных пакетах и т.д.
 instdir  по умолчанию / и указывает каталог, где
пакеты будут инсталлированы.  instdir  - это также
каталог установленный  chroot (2) 
  перед запуском
сценария установки пакета, который говорит сценарию
рассматривать  instdir  как главный каталог.  Chdir 
изменяет  instdir     на  dir  и  admindir  на
 <dir>/var/lib/dpkg. 
 
 -O | --selected-only 
 
Работать только с пакетами, что выбраны к
установке. Данная пометка делается с помощью  dse lect 
или  dpkg,  когда он управляет пакетами. Когда,
например пакет удаляется, он будет помечен к
инсталляции и т.д.
 
 -E | --skip-same-version 
 
Не устанавливать пакет, если таже версия этого
пакетв уже установлена.
 
 ИНФОРМАЦИЯ О ПАКЕТАХ
 
dpkg  обслуживает некоторую полезную информацию о доступных
пакетах. Эта информация делится на три класса: состояния,
состояния выборки и флаги. Эти величины могут быть
изменены с помощью  dselect.
 
СОСТОЯНИЯ ПАКЕТА 
 
 installed </p>
Пакет распакован и успешно настроен.
 
 half-installed 
 
Установка пакета была запущена, но не закончена по
какой-либо причине.
 
 not-installed 
 
Пакет не установлен в вашей системе.
 
 unpacked 
 
Пакет распакован, но не настроен.
 
 half-configured 
 
Пакет распакован и настройка была запущена, но пока
не завершена по какой-либо причине.
 
 config-files 
 
Только конфигурационные файлы пакета существуют в
системе.
 
 СОСТОЯНИЕ ВЫБОРКИ ПАКЕТА 
 
 install 
 
Пакет выбран к установке.
 
 deinstall 
 
Пакет выбран к деустановке (т.е. вы хотите удалить
все файлы, исключая конфигурационные)
 
 purge  Пакет выбран к очистке (т.е. вы хотите удалить все,
даже конфигурационные файлы)
 
 ФЛАГИ ПАКЕТА 
 
 hold  Пакет помечен как hold и не управляется  dpkg  , пока
включена опция  --force-hold. 
 
