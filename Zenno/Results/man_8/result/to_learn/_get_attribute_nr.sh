 get_attribute_nr (8) >>  get_attribute_nr  (8)   ( Русские man: Команды системного администрирования ) 
   
 НАЗВАНИЕ 

 get_attribute_nr  - Работа с атрибутами системы RSBAC.
   
 СИНТАКСИС 

 get_attribute_nr   имя_атрибута 
 
   
 ОПИСАНИЕ 

 get_attribute_nr  возвращает числовое значение атрибута RSBAC по его названию.  
 
   
 ПАРАМЕТРЫ 

При работе с  get_attribute_nr  используется единственный параметр,  имя_атрибута , являющийся названием атрибута RSBAC.
 
   
