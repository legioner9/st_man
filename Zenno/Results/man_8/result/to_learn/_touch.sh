 ПРИМЕЧАНИЯ 

 Пользователи, знакомые со средой BSD, могут обнаружить, что для утилиты
 touch  опция  -f  принимается, но игнорируется. Опция  -f 
не нужна, потому что  touch  успешно выполнится для всех файлов,
принадлежащих пользователю, независимо от прав доступа к ним. 

