 ИМЯ 

tracepath, tracepath6 - показывает маршрут по сетевым узлам с MTU
   
 СИНТАКСИС 

 
 tracepath   адрес  [  порт ] 
 
   
 ОПИСАНИЕ 

 

Показывает маршрут до указанного  адреса  с MTU.
Программа использует указанный  порт  UDP или другой случайный порт.
Она похожа на  traceroute , только не требует привилегий
суперпользователя и не имеет расширенных настроек.
 

 tracepath6  хорошо замещает  traceroute6 
и является классическим примером приложения с очередью ошибок.
Ситуация с  tracepath  хуже, потому что коммерческие
IP маршрутизаторы не возвращают достаточно информации в сообщениях ICMP об ошибке.
Возможно, эта ситуация изменится, когда они будут обновлены.
На данный момент они используют метод Ван Якобсона (Van Jacobson), меняя диапазон
портов UDP для поддержки журнала трассировки.
   
 ВЫВОД 

 

 
 root@mops :~ # tracepath6 3ffe:2400:0:109::2
 1?: [LOCALHOST]                              pmtu 1500
 1:  dust.inr.ac.ru                   0.411ms
 2:  dust.inr.ac.ru        asymm  1   0.390ms pmtu 1480
 2:  3ffe:2400:0:109::2               463.514ms reached
     Итого: pmtu 1480 hops 2 back 2
 

 

Первая колонка показывает TTL шагов с двоеточием.
Обычно значение TTL получается из сетевого отклика,
но иногда отклик не содержит необходимой информации и
делается попытка предположения данных. В этом случае после номера выводится ?.
 

Вторая колонка показывает сетевой сегмент, который ответил на тест.
Это либо адрес маршрутизатора или слово [LOCALHOST], если
пакет не был отправлен в сеть.
 

Остальная часть строки содержит различную информацию о сегменте маршрута. 
Как правило, это содержимое RTT.
Кроме того, может быть показан MTU сегмента если он меняется.
Если маршрут асимметричен или тест закончится прежде, чем он достигнет сегмента, разница
между числом сегментов в прямом и обратном направлении показывается
после ключевого слова async. Эта информация не точная.
В указанном примере в третьей строке показывается асимметрия 1, потому что первый тест
с TTL 2 был отклонён первым сегментом в соответствии с обнаружением маршрута с MTU.
 

Последняя строка суммирует информацию обо всех маршрутах до указанного адреса,
показывает общее значение MTU, количество сегментов до адреса и свою
предполагаемую оценку количества сегмента из адресата к нам, которые могут
различаться когда маршруты асимметричны.
   
