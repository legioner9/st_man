 БЕЗОПАСНОСТЬ 

 

Нет сообщений о найденных проблемах в безопасности.
 

Это стало возможно в связи с особенностями реализации.
 tracepath  не требует привилегий для выполнения, в отличие от
 traceroute ,  ping  и других подобных программ.
 tracepath  может быть выполнен любым пользователем, имеющим доступ
к сети, достаточно послать датаграмму UDP на указанный адрес,
используя заданный порт.
   
 ДОСТУПНОСТЬ 

 

