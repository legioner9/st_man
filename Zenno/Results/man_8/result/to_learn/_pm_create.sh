 pm_create (8) >>  pm_create  (8)   ( Русские man: Команды системного администрирования ) 
   
 НАЗВАНИЕ 

 pm_create  - Работа с классами модуля PM системы RSBAC.
   
 СИНТАКСИС 

 pm_create  class mode файл(ы)
 
   
 ОПИСАНИЕ 

 pm_create  - Создает файлы определенного класса, используя системный вызов  rsbac_pm_create . 
 
   
 ПАРАМЕТРЫ 

 
 pm_create  принимает следующие параметры: 
 class  - класс объекта. 
 mode  - дискретный режим (например 644). 
 файл(ы)  - имя файла или множества имён файлов. 
 
 
   
