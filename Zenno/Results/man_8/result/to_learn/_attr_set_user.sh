 attr_set_user (8) >>  attr_set_user  (8)   ( Русские man: Команды системного администрирования ) 
   
 НАЗВАНИЕ 

 

 attr_set_user  - Установка атрибутов пользователя.
   
 СИНТАКСИС 

 

 attr_set_up  пользователь атрибут  [позиция]  значение
 

 attr_set_up   [ключи]  пользователь log_user_based  [список_запросов] 
   
 ОПИСАНИЕ 

 

Программа  attr_set_user  предназначена для установки значения требуемого атрибута у выбранного пользователя.
   
 КЛЮЧИ 

 
 -p  вывести список результирующих запросов. 
 -a  добавить значение, но не акцептировать изменения. 
 -m  удалить значение, но не акцептировать изменения. 
 
   
 АТРИБУТЫ И ЗНАЧЕНИЯ 

 
 log_user_based 
Журналирование событий системы базирующееся на пользовательских запросах.
 

Значение: битовая строка для всех Запросов.
Дополнительно может использоваться с параметром  имя_запроса .
Значениями, в данном случае, являются  0  = нет,  1  = да.
 

Приемлимыми являются следующие  имена_запросов :
 

ADD_TO_KERNEL, ALTER, APPEND_OPEN, CHANGE_GOUP, CHANGE_OWNER, CHDIR, CLONE, CLOSE, CREATE, DELETE, EXECUTE, GET_PERMISSION_DATA, GET_STATUS_DATA, LINK_HARD, MODIFY_ACCESS_DATA, MODIFY_ATTRIBUTE, MODIFY_PERMISSIONS_DATA, MODIFY_SYSTEM_DATA, MOUNT, READ, READ_ATTRIBUTE, READ_OPEN, READ_WRITE_OPEN, REMOVE_FROM_KERNEL, RENAME, SEARCH, SEND_SIGNAL, SHUTDOWN, SWITCH_LOG, SWITCH_MODULE, TERMINATE, TRACE, TRUNCATE, UMOUNT, WRITE, WRITE_OPEN.
 

Значения запросов вы можете найти в описании ADF.
 mac_categories 
64-значное строчное множество битов для всех категорий.
 

Значение: идентификатор RC-типа ( 0-63 ).
 

Дополнительно может использоваться  с указанием номера изменяемого бита:  mac_categories   позиция .
 

Значениями являются  0  = нет,  1  = да.
 pm_role 
PM-роль пользователя в системе.
 

Значение:  0  = пользователь,  1  = офицер безопасности,  2  = офицер защиты данных,  3  = TP-менеджер,  4  = системный администратор,  5  = наследуемое.
 pm_task_set 
Набор PM-задач.
 

Значение:  pm-task-list-ID  (Идентификатор списка PM-задач (положительное целое число)).
 pseudo 
 

Значение псевдонима для идентификатора пользователя (положительное целое число).
 

Используется при регистрации событий системы вместо идентификатора пользователя.
 rc_def_role 
RC-роль по-умолчанию.
 

Значение: идентификатор RC-типа ( 0-63 ).
 system_role 
Системная роль пользователя.
 

Значение:  0  = пользователь,  1  = офицер безопасности,  2  = администратор.
 security_level 
Уровень классификации безопасности.
 

Значение:  0  = неопределенный,  1  = конфиденциальный,  2  = секретный,  3  = сверхсекретный,  254  = наследованный, максимальный уровень  252 .
 
   
