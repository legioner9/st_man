 БЕЗОПАСНОСТЬ 

 

 arping  требует возможности выполнения CAP_NET_RAWIO.
Это не рекомендуется использовать с битом set-uid root,
т.к. позволяет пользователям модифицировать ARP кэш на окружающих системах.
   
