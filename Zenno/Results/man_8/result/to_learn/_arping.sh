 ИМЯ 

arping - отправляет ARP-запрос окружающим хостам
   
 СИНТАКСИС 

 
 arping  [  -AbDfhqUV ]  [  -c  счетчик ]  [  -w  дедлайн ]  [  -s  отправитель ]   -I  интерфейс   адресат 
 
   
 ОПИСАНИЕ 

 

Отправляет ARP-запрос устройству с MAC  адресат  через сетевой интерфейс  интерфейс ,
используя адрес отправителя  отправитель .
   
 ОПЦИИ 

 
 -A 
Аналогично  -U , но используются пакеты ARP REPLY вместо
ARP REQUEST.
 -b 
Отправляет только широковещательные пакеты уровня MAC. Обычно  arping  сначала
отправляет широковещательные пакеты, а после приема ответа - только адресату.
 -c  счетчик 
Останавливает отправку после отсылки  счетчик  пакетов
ARP REQUEST. С 
 дедлайн 
опцией,  arping  ожидает
 счетчик  пакетов ARP REPLY, пока не истечет указанное время.
 -D 
Режим дублированного обнаружения адреса. См. 
RFC2131, 4.4.1.
Возвращает 0, если дублированное определение адреса прошло успешно, т.е. ответы не приняты
 -f 
Завершает работу после первого приема ответа.
 -I  интерфейс 
Наименование сетевого устройства (интерфейса), через который отправляются пакеты ARP REQUEST.
Указание  интерфейс а для этой опции обязательно.
 -h 
Отображает страницу помощи и завершает работу.
 -q 
Вывод не отображается.
 -s  отправитель 
IP адрес отправителя, использующийся в ARP пакетах.
Если эта опция не указана, используется следующий адрес отправителя:
 
 
 * 
В режиме сдублированного определения адреса (с опцией  -D ) - 0.0.0.0.
 * 
В обычном ARP-режиме (с опцией  -U  или  -A )
используется  адресат .
 * 
В других случаях адрес вычисляется по таблице маршрутизации.
 
 

 -U 
Предоставляет содержимое кэша ARP для обновления ARP кэшей соседних систем.
Ответы не ожидаются.
 -V 
Показывает номер версии программы и завершает работу.
 -w  дедлайн 
Указывает таймаут в секундах перед тем, как
 arping 
завершит работу и отобразит сколько пакетов было отправлено или принято.
В этом случае  arping 
не останавливается после отправки
 счетчик 
пакетов, он ожидает пока
 дедлайн 
истечет или пока не будет принято
 счетчик 
ответов.
 
   
